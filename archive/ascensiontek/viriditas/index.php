<h1>Viriditas</h1>
<p>&quot;Viriditas&quot; is a company which may potentially
	develop out of <a href="../gsh/index.php">Green Steel
	Homes</a>.&nbsp; It would provide
	permaculture and native plant gardening services,
	and land rehabilitation services.</p>
<p>These activities will naturally form part of ecovillage
	development, and originally
		they were included as part of GSH.&nbsp; However,
		their inclusion was diluting the focus of that
		business, which would have needed a more general
		name.&nbsp; In
		the early stages I would prefer to focus
		on construction of ecovillage and sustainable
		houses, then offer the services described here
		down the track if appropriate.</p>
<p>&nbsp;</p>
<h2>Gardening and Landscaping Services</h2>
<p>This includes design and creation services for both
	permaculture and native plant gardens for single-house
	lots, ecovillages, eco-resorts or other developments.</p>
<p>The types of gardens we create will be highly practical
	- not the sort with garden gnomes, pebbled pathways
	or marble fountains.&nbsp; Other people do that.&nbsp; Our
	gardens are designed to create a positive benefit
	for the environment.</p>
<p>There are two main types of gardens we will provide.&nbsp; A
	development may include one of these or a combination
	of both.</p>
<h3>Permaculture </h3>
<p>A well-designed permaculture garden can greatly
	increase the value of a property, because it then
	becomes able to produce food for the residents.&nbsp; While
	this comes with a small labour cost, it does reduce
	the residents' weekly expenses.&nbsp; It will also
	contribute to their health, since the food produced
	by the garden will be both fresh and organic (chemical-free).</p>
<p>Food grown in a permaculture garden also does not
	carry any embodied energy, since no fuel is needed
	to transport it.&nbsp; Plus you have the added security
	of knowing that, no matter what happens, at least
	you will still be able to eat!</p>
<h3>Native Plants</h3>
<p>The second type of garden we will build for people
	are native plant gardens with a high food value
	to local wildlife.&nbsp; This benefits the environment
	because local wildlife, especially birds, are excellent
	agents for plant propagation, and if there's one
	thing the world needs more of right now it's more
	plants (and, possibly, love).&nbsp; Our native plant
	gardens will also be full of colourful flowers,
	which is good for humans, as well as bees who are
	also good plant propagators.</p>
<p>&nbsp;</p>
<h2>Land Rehabilitation and Reforestation</h2>
<p>The function of this component of the business is
	to replant thousands of trees on previously-cleared
	land, which forms part of a wider strategy
	aimed at removing carbon from the atmosphere.</p>
<p>&nbsp;</p>
<h3>Carbon removal strategy </h3>
<p>(This is just a brief explanation of AscensionTek's
	atmospheric carbon removal strategy until a more
	detailed article is added to the website.)</p>
<p>The most important global environmental project
	right now is the removal of carbon from the atmosphere,
	hence the existence of <a href="http://news.bbc.co.uk/2/hi/science/nature/6345557.stm" target="_blank">Branson's
	Earth Challenge Prize</a>.</p>
<p>The best technology we have for removing carbon
	from the atmosphere is vegetation.&nbsp; It has been
	said that the cycling carbon through the biosphere
	is very slow, as compared with just burying it
	(carbon sequestration).&nbsp; However, this only applies
	if the planet is in a steady state.&nbsp; Our goal
	is to significantly increase the mass of the biosphere,
	effectively converting atmospheric carbon into
	plants and animals.&nbsp; This is a much safer and
	much more beneficial outcome.&nbsp; Trees and other
	plants provide food, medicines, shelter and building
	materials for all kinds of animals including humans.</p>
<p>Our strategy for lowering carbon levels in the atmosphere
	is deceptively simple.&nbsp; We need
	to do two main things:</p>
<ul>
	<li>Plant millions of trees.</li>
	<li>Stop cutting down trees.</li>
</ul>
<p>&nbsp;</p>
<h3>Services</h3>
<p>There are several elements to this component of
	the business:</p>
<ul>
	<li><strong>Mass production of native plant species.</strong>&nbsp; This
		may be achieved with automated farming techniques
		currently in development for the agricultural
		industry, including automated greenhouses.</li>
	<li><strong>Land rehabilitation via mass planting
			of native plant species.</strong>&nbsp; This
		may be achieved using purpose-built robots.</li>
	<li><strong>Ecosystem research and modelling.</strong>&nbsp; Regional
		ecosystems need to be well-understood, as well
		as the effects of mass plantings on existing
		ecosystems, before major land rehabilitation
		can be commenced.&nbsp; Computers can be used to
		model complex ecosystems and to investigate
		effects.</li>
</ul>
<p>In the first stage of this aspect of the business,
	land rehab services would be provided to the usual
	customers such as quarries, mines, and land developers.&nbsp; Eventually
	we would also like to be rehabilitating old sheep
	and cattle properties, expired farmland, etc.&nbsp; Our
	major goal would be to institute global mass replanting
	programs.</p>
