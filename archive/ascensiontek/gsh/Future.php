<h2>Geodesic Domes</h2>
<p>Apart from conventional rectilinear houses, I have
	also been investigating geodesic domes.&nbsp; Geodesic
	domes have a number of advantages:</p>
<ul>
	<li>Improved energy-efficiency - the dome shape tends
		to mitigate thermal layering.</li>
	<li>Structural strength and stability.&nbsp; The self-supporting
		shape does not require any structural internal walls. </li>
	<li>Storm resistance.&nbsp; The curved shape allows
		high-speed winds to flow around the building with
		minimal resistance.</li>
</ul>
<p>The disadvantages of geodesic domes include:</p>
<ul>
	<li> Increased complexity of construction.</li>
	<li>Increased material wastage due to triangular wall
		panels and windows. </li>
	<li>Difficulty fitting rectangular furniture into
		curved or angular floor plans, or hanging paintings
		or curtains against curved/angled walls.</li>
</ul>
<p>In spite of these challenges domes have a certain
	futuristic aesthetic appeal along with the advantages
	described above, and with clever design the disadvantages
	can be minimised.&nbsp; Although I would not want
	to start GSH with domes, this may be an option in
	the future.</p>
<p>A compromise which offers the advantage of a rectangular
	floor area while still retaining most of the advantages
	of a dome, is a hemi-cylindrical shape.&nbsp; This,
	too, may become an option in the future. </p>
