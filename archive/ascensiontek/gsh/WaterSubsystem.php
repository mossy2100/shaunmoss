<h2>Water Quality</h2>
<p>Water quality from either mains supply or rain water
	tanks is rarely suitable for drinking.&nbsp; This
	house includes 4 levels of filtration and provides
	extremely pure water.</p>
<p>1.&nbsp; <a href="http://www.rainharvesting.com.au/blue_mountain_all_steel_gutter_mesh.asp" target="_blank">Blue
Mountain Gutter Mesh</a> has approximately a 5mm aperture
and filters leaves, twigs, gumnuts, large insects,
frogs, rats and mice from entering the water supply.&nbsp; It
also prevents mosquito breeding in the gutters, since
they remain clear and warm pools cannot form, and reduces
the possibility of gutters catching fire.</p>
<p>2.&nbsp; The <a href="http://www.clarktanks.com.au/" target="_blank">Clark
		Tanks</a> come with very fine mesh
	(&lt;1mm aperture) which prevents mosquitos and small
	particles entering the tank.</p>
<p>3.&nbsp; A <a href="http://purewater.net.au/whole_house.htm" target="_blank">Pure
		Water Systems &quot;Whole of House&quot; carbon filter</a> removes 


 &quot;chlorine, trihalomethanes, chemical pollutants
 and associated tastes and odours&quot;.&nbsp; This
 has been used for the whole water supply to the house,
 rather than drinking water only, mainly so that
 chlorine is not present in shower water.</p>
<p>4.&nbsp; Finally, an under-sink filter removes bacteria
	and pretty much everything else you can think of.&nbsp; This
	provides high-purity water for drinking and cooking.</p>
