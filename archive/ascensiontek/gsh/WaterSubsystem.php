	you are.&nbsp; However, considering drought conditions
	and the rapid growth of Australia's population,
	it is unlikely to remain that way.&nbsp; Furthermore,
	most water supplies, whether from town water, bore
	water or rain water, is tainted in some way by
	chemicals or pathogens.&nbsp; A
	modern purification system is essential for good
	health, no matter where the water comes from.</p>
