<p>This house is extremely water efficient, mainly
	because of the low water requirements of the washing
	machine (36 litres per wash) and toilet (0 litres).&nbsp; The
	total requirements of the house, including water
	for gardens, should be less than 120,000 litres
	per year.&nbsp; The grey water recycling system recycles
	water from the showers and laundry, a total
	of about 200L per day, which is more than enough
	for the laundry, car washing and gardens.&nbsp; The
	rainwater tank therefore only needs to supply water
	for drinking, cooking, cleaning and showering,
	a total of about 200L per day or 6,070L per month,
	which means it can store enough
	water for almost 4 months supply.&nbsp; In
	theory the mains water should never be required,
	however we connect it as a precaution.</p>
