<h1>Investor Questions</h1>
<h2>What makes GSH different from any other house building
	company?</h2>
<p><strong>1.&nbsp; The software.</strong>&nbsp; The
	manager of this company (me) is a software developer
	 with 20 years commercial experience, many of which
	involved writing software for the steel building
	industry.&nbsp; Our software will give customers the
	flexibility of creating their own design, providing
	accurate quotes in real time, without requiring
	the expensive services of an architect or engineer.&nbsp; The
	software will greatly streamline the construction
	process and all of our business processes, automatically
	sending the correct information to all participants
	at the right time.&nbsp; The result is a high-quality
	product that suits the customer, completed quickly
and within budget.</p>
<p><strong>2.&nbsp; Fully integrated sustainable design.</strong>&nbsp; There
	are other building designers around calling themselves
	sustainable, when their only point of uniqueness
	is following energy efficiency guidelines.&nbsp; Our
	homes, however, will be as environmentally-friendly
	as possible from the ground up, incorporating the
	most sustainable building materials, design methodologies
	and environmental technologies.&nbsp; These
	types of dwellings represent one of the fastest-growing
	segments of the housing market.&nbsp; As climate
	change and the environmental movement progress,
	people are become increasingly savvy about sustainable
	technologies and will demand more from new houses.&nbsp; We
	will provide it.</p>
<p><strong>3.&nbsp; Strength and toughness.</strong>&nbsp; Our
	homes will be solid steel and extra-strong, built
	for the next wind code higher than is required
	by council.&nbsp; This
	will ensure that they survive the increases in
	storm intensities and wind force expected as a
	result of climate change.</p>
<p><strong>4.&nbsp; Smart housing concepts.</strong>&nbsp; At
	present in most Australian homes, internet is an
	&quot;add-on&quot;, requiring the purchase of a modem
	and possibly also networking equipment.&nbsp; We
	believe internet should be as transparent as electricity,
	always on, and available everywhere in the house.&nbsp; Broadband
	internet and wireless networking is now commonplace,
	and can be integrated into a house very easily
	providing wireless internet access anywhere inside,
	and in some outdoor areas as well.&nbsp; Our homes
	will also include a &quot;house
	server&quot;,
	a built-in computer that records information about
	energy and water consumption, connects with
	security systems, and can be used to run a range
	of house management software.</p>
<p><strong>5.&nbsp; Customer service.</strong>&nbsp; Everyone
	says it, but not everyone really does it.&nbsp; Rather
	than just sell a product, we will seek to enter
	into a relationship with our customers that will
	include ongoing maintenance of their home during
	its lifetime.&nbsp; Our
	website will provide a range of after-market services
	to our customers, including consultancy,
	repair requests, upgrade
	options, and a wide range of solutions and advice
	to help people live more sustainably.</p>
<p><strong>6.&nbsp; Marketing.</strong>&nbsp; Advertising
	is a highly effective tool which is often under-utilised,
	especially by builders who seem to advertise
	very rarely if at all.&nbsp; We will
	implement a sustained marketing plan using internet,
	TV, radio and newspaper.&nbsp; Our goal
	is mass production of homes, not just a handful
	a year, but ultimately hundreds or even thousands,
	and to achieve this we need to be well known.&nbsp; This
	scale of service is essential for successful sustainable
	development of the region, and also in order to
	make the homes more affordable.&nbsp; The
	cost of extensive marketing will be offset many
	times over by the large number of sales.</p>
<p>&nbsp;</p>
