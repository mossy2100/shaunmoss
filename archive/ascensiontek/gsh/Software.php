<h1>Software Development</h1>
<p>AscensionTek will have a very strong IT department,
	mainly because computer software and the internet
	will play such a large role in the operation of
	the company.&nbsp; We will be developing tools for
	our own business operation, including tools for
	designing and building ecovillages and other green
	developments, and managing their operation.</p>
<p>Of course, this
		kind of software will be valuable to other
	developers as the ecovillage movement takes off.&nbsp; We
	can capitalise on this by developing
	our software into off-the-shelf products.&nbsp; As
	the company grows, our software department can
	expand into new areas, such as robotics, mining
	and space. </p>
<p>There are a range of advantages in having a software
	products division in the company:</p>
<ol>
	<li> <strong>Domain knowledge.</strong>&nbsp; Developing
		software invariably generates what is known
		as <em>domain
		knowledge</em>, knowledge that programmers
		accumulate with each project that they work
		on.&nbsp; When
		building an application for a specific industry,
		programmers absorb knowledge
		(often very detailed knowledge) about that
		industry, or at least one specific aspect of
		it.&nbsp; By
		developing software for companies
		in the property development, construction,
		robotics, mining, space, scientific, engineering,
		and other
		related industries, AscensionTek will automatically
		absorb domain knowledge from fields that will
		support its activities in space later in the
		century.</li>
	<li><strong>Profit.</strong>&nbsp; Very few product
		categories have profit margins as high as software, <em>if </em>the
		software is high quality, <em>and </em>suited
		to a sufficiently large number of users.&nbsp; Our
		goal will be to develop world-class software
		programs for use by companies in the industries
		mentioned, to create another income stream. </li>
	<li><strong>Maximizing business efficiency.</strong>&nbsp; Software
		is powerful stuff if done properly.&nbsp; A software
		system can be a valuable aid in the management
		of a business, organising users, staff, suppliers
		and clients into a co-ordinated and efficient team,
		maximising the effectivenes of a business.&nbsp; AscensionTek
		means to be effective on a global scale, and
		with an awesome software team in place, we will develop
		systems that can integrate a large number of
		business activities into a world-changing
		machine.</li>
	<li><strong>Leverage.</strong>&nbsp; Software development
		is my main area of training and experience,
		so it makes sense to use it for commercial
		advantage.</li>
    <li><strong>Relationships.</strong>&nbsp; Having
    	a software products division will be a catalyst
    	for building relationships with a wide range
    	of interesting companies.&nbsp; These
    	relationships may come in handy when we proceed
    	to space development, which will require many
    	co-operating partners.</li>
</ol>
<p>During Phase 1 of the business, the emphasis will
	be on developing software products for the property
	development and construction industries.</p>
