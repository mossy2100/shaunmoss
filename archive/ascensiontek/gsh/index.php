<h1>Welcome to Green Steel Homes</h1>
<p>&quot;Green Steel Homes&quot; (GSH) will build steel-frame
	sustainable homes.&nbsp; This is the
	first company and brand to be started by AscensionTek,
	which functions as the parent company (I've chosen
	this structure so that AscensionTek will have the
	flexibility to become involved in other industries
	in the future, such as those described in <a href="../bizplan/FutureDev.php">Phase
	2: Future Developments</a>).</p>
<p>&nbsp;</p>
<h2>Schedule of Business Activities</h2>
<p>There are two main components to GSH.</p>
<ol>
	<li> <strong><a href="SustainableHomes.php">Sustainable
				Homes</a>.</strong>&nbsp; These
			will be built with environmentally-friendly
			materials, using sustainable design principles,
		and incorporating environmental technology.</li>
	<li><strong><a href="EcovillageProjects.php">Ecovillage
	Projects</a>.</strong>&nbsp; Design, construction
	and operation of ecovillage-style developments
	of all sizes. </li>
</ol>
<p>&nbsp;</p>
<h3>Related Services </h3>
<p>During the development of GSH, we will be on
	the look-out for related business opportunities.&nbsp; The
	most obvious of these is provision of <strong>consultancy</strong>	services.&nbsp; Once
	we have some experience with sustainable building
	design and especially ecovillage design, we will
	be in a position to advise others wanting to do
	this. </p>
<p>Another potential opportunity is <strong>software</strong>.&nbsp; GSH
	will have a strong IT department, and will be developing
	a variety of advanced programs related to construction,
	property development and real estate.&nbsp; Considering
	the high profit margins on software products, there
	may be interesting commercial opportunities to
	market these programs to other companies.</p>
<p>Yet a third opportunity will be <strong>technology
		spin-offs</strong>.&nbsp; We
	have discovered that several technologies
		which we would have liked to include in a GSH
		do not exist yet.&nbsp; When the company has the
		resources, we will establish an R&amp;D workshop
		and develop some of these, both to improve
		the homes, and to offer as products.</p>
