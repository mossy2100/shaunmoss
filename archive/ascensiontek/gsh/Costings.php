<h2>Subsequent Expenses</h2>
<h3>Consultancy fees </h3>
<p>I'm a big believer in using experts.&nbsp; In
	the long run, it's much cheaper to talk to someone
	who already knows the answer rather than trying
	to figure out everything on your own, because many
	mistakes can be avoided.&nbsp; With a small, 5-person
	team, many tasks that need to be completed by the
	business will need to be outsourced, which will
	be a significant expense.&nbsp; These may include:</p>
<ul>
	<li><strong>Ecovillage consultant.</strong>&nbsp; There
		is an ecovillage consultancy up at Conondale (Sunshine
		Coast) called <a href="http://www.ecologicalsolutions.com.au/" target="_blank">Ecological
		Solutions</a> which has advised several ecovillages
		in the region.&nbsp; These guys will have a lot of
		valuable advice and ideas for us. </li>
	<li><strong>Structural engineering.</strong>&nbsp; It's
		possible that this cost can be avoided by partnering
		with an existing sustainable building company
		that has a product we would like to use in
		our ecovillages (for example <a href="http://www.sustainablebuildings.com.au/default.aspx" target="_blank">Sustainable</a> or <a href="http://www.civicsteelhomes.com.au/" target="_blank">Civic
		Steel Homes</a>). &nbsp; Otherwise, having our new
		building designs approved by engineers can
		cost $tens of thousands.</li>
	<li><strong>Environmental consultant.</strong>&nbsp; We
		will need expert advice on how best to manage a piece
		of land in order to preserve and enhance its natural
		wildlife.&nbsp; This will include a flora and fauna
		survey of the site.</li>
	<li><strong>Horticultural consultant.</strong>&nbsp; If
		the site is being rehabilitated, we will need
		expert advice on the types of native flora that can
		safely be planted.&nbsp; The best people for this
		role may be <a href="http://www.fairhill.com.au/" target="_blank">Fairhill
		Nurseries</a>, native plant experts located on the
		Sunshine Coast.</li>
	<li><strong>Permaculture consultant.</strong>&nbsp; We
		will require advice about how best to organise
		our land for effective food production.&nbsp; Again,
		this may be a role for Ecological Solutions. </li>
	<li><strong>Energy consultant.</strong>&nbsp; Since
		we plan to produce all our own electricity, we will
		need advice about how to integrate our solar panels
		and wind turbines into a site-wide energy grid, and
		how to connect this to the main grid.</li>
	<li><strong>Water and waste management consultant.</strong>&nbsp; We
		will need expert advice on how to correctly manage
		our grey and black water as well.</li>
	<li><strong>Network engineer.</strong>&nbsp; A site-wide
		wireless network and information system is not hard
		to design, but it would still pay to talk to an expert.</li>
</ul>
<p>Once
	we've built our first ecovillage we won't necessarily
	need to consult with all these people again,
	especially if we keep good records and document
	our experiences as we go.&nbsp; The consultancy fees
	will (theoretically) be lower with each new project,
	as we gain experience and as we bring more specialists
	into the business.</p>
<p>At this stage, it is hard to estimate the total
	cost of these consultancy fees.&nbsp; I would envisage
	on the order of $50k for the first ecovillage,
	and declining from there.</p>
