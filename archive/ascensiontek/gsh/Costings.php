<h1>Financial Information</h1>
<p>&nbsp;</p>
<h2>Startup Expenses</h2>
<p>I have begun making estimates about what it will
	cost to start this company properly.&nbsp; Below is
	some information about how I arrived at my estimates,
	which have been collected together in <a href="../doc/GSH_Costings.xls" target="_blank">this
Excel spreadsheet</a>.&nbsp; The main expense in starting
the company will be hardware and software. </p>
<p>&nbsp;</p>
<h3>Hardware (Computers and Printers)</h3>
<p>Naturally, everyone will need a computer, plus the
	company will need at least one server.&nbsp; We will
	also require a colour A4 printer for regular business
	printing, plus an A3 and A1 printer for engineering
	and architectural drawings.</p>
<p>&nbsp;</p>
<h3>Software</h3>
<p>Software we need will include Microsoft Office,
	Adobe Creative Suite, Zend Studio, AutoCAD, Revit
	Architecture, and related construction programs.</p>
<p>&nbsp;</p>
<h2>Operating Expenses </h2>
<p>&nbsp;</p>
<h3>Staff</h3>
<p>This will be the largest operating expense, and
	unfortunately unavoidable until robotics and AI
	technology improves.&nbsp; I
	have looked at the minimum team I would need to
	start the company and it looks something like this:</p>
<ol>
	<li><strong>Me.</strong>&nbsp; Writing, programming,
		planning, researching, management, communications,
		and overseeing the development of the software
		and the ecovillages.</li>
	<li><strong>Planner.</strong>&nbsp; This would be someone with experience
		as a project manager in property development, who
		is familiar with things like financial management,
		scheduling, logistics, infrastructure development,
		council applications, dealing with builders and so
		on.</li>
	<li><strong>Green Architect.</strong>&nbsp; An architect
		with experience and/or training in sustainable development.&nbsp; This
		role would primarily involve development of
		innovative designs for sustainable steel-frame
		houses, house modules, and community buildings.&nbsp; This
		role would involve liasing with structural
		and civil engineers, and builders. </li>
	<li><strong>Programmer.</strong>&nbsp; This person would develop the
		web-based software system as described in the
		<a href="GSH_BizSys.php">Green Steel Homes Business
		System</a>.&nbsp; They
		would be conversant with client and server-side
		web and database programming, including online
		communities and integration of multimedia such
		as video and 3d graphics with the web.</li>
	<li><strong>Draftsperson/3d modeller.</strong>&nbsp; This person would
		mainly work with the architect, but also with the
		programmer, producing drawings and 3d models for
		builders and for the website.</li>
</ol>
<p>With a small team like this, and an ambitious project,
	it is essential to hire top people.&nbsp; For this
	reason I estimate the total cost of salaries for
	this lot for one year to be possibly as high as
	$400k.&nbsp; I understand the need to keep costs down
	during the early stages of the business, but I
	also want the company to have a reputation as
	being generous with its salaries.&nbsp; I think it's
better to start off on the right foot.</p>
<p>&nbsp;</p>
<h3>Office Expenses</h3>
<p>I like serviced offices, because then there's a
	whole lot of stuff I don't need to think about,
	like furniture, a receptionist, phone lines, internet
	connections, photocopiers, fax machines, caffeine
	sources, the colour of the carpet and so on.&nbsp; I've
	looked around Brisbane at the rates for services
	offices and a 5-person office in the CBD costs
	about $2500 to $3000 per month.&nbsp; Presumably it
	would be cheaper outside of the CBD.&nbsp; I think
	that, including the costs of telephone, internet,
	photocopying, and other minor incidentals, the
	total office expenses should be maximum $3000 per
	month.</p>
<p>&nbsp;</p>
<h3>Consumables</h3>
<p>There will need to be a budget for day-to-day company
	expenses such as office consumables (printer paper
	and ink, blank CDs, pens and notebooks), petrol
	and other travel expenses, printing (e.g. business
	cards and mail-outs), internet expenses such as
	domain name registration fees and web-hosting,
	company shirts, and the occasional business lunch
	or dinner.&nbsp; This
	should only come to a few hundred dollars per week
	(I have estimated a maximum of $400/week).</p>
<p>&nbsp;</p>
<h2>Total Business Expenses for Year 1</h2>
<p>Please refer to <a href="../doc/GSH_Costings.xls" target="_blank">this
Excel spreadsheet</a> for details.</p>
<p>The total setup costs are mainly computer hardware
	and software, and come to a total of about $50,000.&nbsp; The
	operating costs for the first year come to about
	$430,000 about 90% of which is salaries.&nbsp; Total
	of around $480,000.&nbsp; This does not include any
	property development costs, this is just setting
	up and running the location.&nbsp; Considering there
	are bound to be things I haven't thought of, it
	is probably sensible to allocate about $500,000
	in total.</p>
<p>&nbsp;</p>
