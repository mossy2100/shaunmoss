<h2>Why Ecovillages?</h2>
<p>Ecovillages represent a truly tremendous business
	opportunity with multiple advantages:</p>
<h3><strong>1. Ecovillages are good for Earth.</strong></h3>
<p>Ecovillages have been proven over the centuries
	as an effective approach to sustainable habitation
	of Earth. Development of new technologies in recent
	years have made ecovillages a much more viable,
	accessible and popular alternative.&nbsp; They reduce
	the load on national water and energy infrastructure,
	and respect, conserve and often rehabilitate the
	natural environment.</p>
<p>Ecovillages are a way of living in
	harmony with Earth, but also with each other.&nbsp; People
	rarely become isolated within an ecovillage because
	of shared goals, activities and facilities, minimising
	issues
	such as depression which are becoming commonplace
	in modern society.&nbsp; This
	type of environment therefore also leads naturally
	to improved physical health, since most physical
	ailments are a result of underlying mental or emotional
	problems.&nbsp; Another
	way that ecovillages contribute directly to the
	health of its residents is through the production
	of fresh, whole organic foods.</p>
<h3><strong>2. Ecovillages and sustainable housing are
		profitable, and increasingly in demand.</strong></h3>
<p>Climate change has brought environmental issues
	to the forefront of the public consciousness, where
	they are likely to remain for decades.&nbsp; The
	general public is beginning to realise the need
	for more sustainable habitation practices, and
	there is a definite market shift towards environmental
	products and services. &nbsp; At the same time, population
	growth and industrialisation in developing nations,
	plus mass displacement caused by rising sea levels,
	is leading to increased demand globally for high-quality,
	safe, sustainable housing solutions. Hence we can
	expect our products and services to be in high
	demand for at least the next few decades, and probably
	indefinitely.</p>
<p>We improve our early profitability by beginning
	in Queensland, the fastest growing state in Australia.&nbsp; Most
	Queensland millionaires built their wealth through
	property investment and/or development.</p>
<h3><strong>3. Ecovillages are good terrestrial analogues
for moonbases or marsbases.</strong></h3>
<p>While most people would probably not consider this
	to be a particularly important advantage, for AscensionTek,
	it is.</p>
<p>There are many common elements between space settlements
	and ecovillages. Like a moonbase or marsbase, an
	ecovillage is a highly independent community which
	produces most or all of its energy, food, and water.
	Both space settlements and ecovillages require
	high energy and water efficiency, water recycling,
	energy production, and small-scale agriculture,
	and each includes shared facilities for things
	like health-care, equipment maintenance, community
	meals, and scientific research. Each also represents
	a small population living and working together,
	thus many of the same human factors issues exist,
	such as consensus decision-making and conflict
	resolution.</p>
<p>Hence, gaining experience with ecovillage design,
	construction and operation over the next 3 or more
	decades will generate valuable insight into moonbase
	and marsbase design, both at the level of the built
	and natural environments, and in the psychological
	factors relevant to small integrated communities.</p>
