<h2>Our First Ecovillage </h2>
<p><a href="<?= $SilvergreenUrl ?>">Silvergreen</a> is
	our first ecovillage project, and has already begun.&nbsp; Ecovillages
	are usually put together by a group of like-minded
	individuals, and therefore do not require a large
	amount of business resources to create (although
	of course this is an advantage!).&nbsp; It means that
	the Silvergreen project can be started now, even
	before the company has sponsorship.</p>
<p>This initial project will help to catalyse the development
	of a core design and management team, as well as
	solutions for sustainable buildings and community
	design.</p>
<p>Because
	the house construction component of the company
	does not yet exist, the current plan for Silvergreen
	is to use a 3rd party builder, and consultants
	for other parts of the design as required.&nbsp; This
	first project will be a big learning experience
	for the company, in which we use mainly outside
	advisors.&nbsp; However, with each successive project
	we will be able to rely more on our own knowledge
	and experience.</p>
<p>&nbsp;</p>
<h2>Progressive Challenges</h2>
<p>Our strategy for the ecovillage development part
	of the business is to progressively increase
	the difficulty of our projects, in terms of size
	and/or habitability challenges.&nbsp; This
	is done for three reasons:</p>
<ol>
	<li>To show that the ecovillage concept can be extended
		to practically every corner of Earth.</li>
    <li>To show that ecovillage design principles are
    	applicable at every scale, from
    	single-house lots to cities.</li>
    <li>As a steady progression towards moonbases and
    	marsbases.</li>
</ol>
<p>This progression must be managed with a degree of
	restraint - each project must be evaluated for
	its economic feasibility and our tendency will
	be to avoid risk as much as possible.&nbsp; However,
	with each project our confidence and knowledge
	will increase, as will our ability to make challenging
	projects successful.</p>
<p>The progression might go something like this:</p>
<ol>
	<li>Small ecovillages with 10-100 houses.</li>
    <li>Large ecovillages with 100-1000 houses.</li>
    <li>Urban and suburban ecovillages within cities. </li>
    <li>&quot;Ecotowns&quot;, with as many as 100,000 houses.</li>
    <li>Ecovillages and ecotowns in the desert - important
    	for Australia and elsewhere, offering challenges
   	in local production of food and water.</li>
    <li>  &quot;Mining ecotowns&quot;  - remote but
    	sustainable mining communities.&nbsp; Remote
    	mining towns are a good analogue for future
    	moonbases and marsbases.</li>
    <li>Ecovillages and ecotowns in polar regions -
    	challenges in environment control (see <a href="../bizplan/FutureDev.php">Phase
    	2: Future Developments</a>). </li>
    <li>&quot;Ecocities&quot; with populations of 1,000,000 or
    	more.&nbsp; Planned
    	cities designed around ecovillage principles.</li>
</ol>
<p>At the moment it may seem hard to imagine that one
	company could consider taking on this range of
	projects.&nbsp; However, over a 100 year period, with
	continually developing technology, locations
	around the world, and a great system, this is exactly
	what will happen.</p>
