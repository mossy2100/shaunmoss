<h1>Software and Business System</h1>
<p>This is probably the most important page in the
	whole business plan.</p>
<p>It describes the business system for selling sustainable
	steel-frame homes and managing ecovillages using
	the internet.&nbsp; The
			system is mainly designed to sell homes
		within ecovillages, but can also be used for
	selling homes to be constructed on a customer's
	own land.</p>
<p>To give you the gist, here's a diagram that gives
	an  overview (click to open PDF).&nbsp; The
	explanation follows below - it may be helpful to
	switch back and forth between the two.</p>
<p>&nbsp;</p>
<p align="center"><a href="../doc/GSH_BizSys.pdf" target="_blank"><img src="../images/GSH_BizSys.png" alt="GreenSteel Homes diagram" width="400" height="555" border="1"><br>
Click to enlarge (131kB PDF)</a></p>
<p>In the diagram, light blue rectangles denote web-based
	software components, yellow and orange ellipses
	indicate documents and other deliverables (orange
	being most important), and blue ellipses are the
	major construction elements.</p>
<p>&nbsp;</p>
<h2>1. Creating Ecovillage Projects</h2>
<p>The first major software component is an ecovillage
	community website, where users can create their
	own ecovillage projects - these users become &quot;project
	leaders&quot;.&nbsp; Other
	visitors to the site can sign up as being interested,
	and will then receive updates about that ecovillage.&nbsp; Each
	ecovillage project will have an online forum attached
	to it, and the project leaders will be able to
	organise online and in-person meetings for their
	project.</p>
<p>A range of tools are provided by the site to help
	the ecovillage leaders with their project management
	and design.&nbsp; These will include:</p>
<ul>
	<li>an automated system that leads them step-by-step
		right through the creation of their ecovillage
		from conception to implementation</li>
    <li>a subdomain for their ecovillage website</li>
    <li>online help and downloadable e-books about how
		to make an ecovillage successful</li>
    <li>financial tools to help estimate costs and
    	projections</li>
    <li> interactive site maps where the leaders and
    	their team can layout their buildings</li>
    <li>calculators for estimating energy, water
    	and food requirements for the ecovillage</li>
    <li>tools for
    	communication and collaboration within a group,
    	and with other groups</li>
    <li>an events calendar</li>
    <li>an online chat room for meetings </li>
    <li>file storage area for documents and drawings</li>
</ul>
<p>New tools will be added frequently, and the site
	will evolve in accordance with the requests of
	its members.&nbsp; Designing ecovillages has never
	been easier!</p>
<p>There will be a modest fee to start an ecovillage
	project, but not to join one.&nbsp; The reason for
	the fee will not be so much to generate income
	(most profit will come from sales of house-and-land
	packages), but to prevent the database from becoming
	cluttered with whimsical projects.&nbsp; Some income
	will also be generated through advertising of environmental
	products and services from other companies. </p>
<p>&nbsp;</p>
<h2>2.&nbsp; Feasibility Studies </h2>
<p>Some of the ecovillage projects will begin to gain
	real momentum, and this will become apparent to
	the planners and other people who are part of GSH.&nbsp; Some
	groups will want to set up their own ecovillages
	in the traditional way, i.e. sourcing their own
	land and designing their own buildings and so on,
	which is fine (the more ecovillages the better),
	but in many cases the groups will want us to do
	this for them, which is of course the main service
	provided by the business.</p>
<p>For those ecovillage groups who have become established
	and who want to begin making their plans into a
	reality, GSH conducts a feasibility study.&nbsp; This
	is made much easier by the software tools and calculators
	which are already available - in fact, we will
	develop these tools to the point where we can accurately
	estimate the cost of an ecovillage project and
	simulate its operation over the first, say, 10
	years.</p>
<p>After a feasibility study, if an ecovillage project
	looks like a good business proposition for us
	then we go to the next stage.</p>
<p>&nbsp;</p>
<h2>3.&nbsp; Planning</h2>
<p>This is where the GSH planners and the ecovillage
	project participants work together to firm up the
	plans for an ecovillage.&nbsp; All aspects such as
	financial, legal, environmental and social are
	examined.&nbsp; A list of buildings and other infrastructure
	items is made and a schedule for construction.&nbsp; A
	list of criteria for a suitable block of land is
	also made.</p>
<p>In this stage, GSH planners may begin to attend
	the ecovillage group's meetings, to  guide
	the group, offer advice, learn about the group's
	priorities, and to make sure they are on the right
	track.</p>
<p>&nbsp;</p>
<h2>4.&nbsp; Land Acquisition and Administration </h2>
<p>GSH and the ecovillage group find a block that they
	agree on, which GSH then purchases, either outright
	or using finance depending on the circumstances.&nbsp; We
	then take care of all the council requirements,
	development applications, subdivision, rezoning
	if necessary, and so on.&nbsp; The residents won't
	have to worry about any of this.</p>
<p>With the land acquired, maps based on aerial photography
	are added to the website for the ecovillage project,
	to replace any &quot;working&quot; maps that they've
	been using to layout their ecovillage.&nbsp; The group
	can adjust their site layout using the real
	maps, with assistance from our planners.&nbsp; A collection
	of photographs will also be added to the website
	for the benefit of people who may not have been
	able to visit it themselves.&nbsp; Using topographical
	data acquired  with the aerial photography,
	a 3d model of the site terrain is constructed,
	into which models of the houses and other buildings
	can be placed.&nbsp; Potential residents will then
	be able to &quot;explore&quot;
	their ecovillage in cyberspace (like in a first-person
	computer game).</p>
<p>Again, in co-operation with the participants, GSH
	will mark out house sites on
	the ecovillage land so that:</p>
<ul>
	<li>there are enough lots for their requirements</li>
    <li>they are large enough</li>
    <li>they conform to state/county/shire/whatever
    	regulations</li>
    <li>they don't take up too much of the overall
    	land area, with the majority of land preserved
    	for nature, and some allocated for community
    	buildings</li>
</ul>
<p>&nbsp;</p>
<h2>5.&nbsp; House Designs and Sales</h2>
<p>Would-be ecovillage residents can now visit
	the website, select their house lot from an interactive
	map of the ecovillage, and design their house online.&nbsp; They
	may select from some preset floor plans, or they
	can design their own home on-screen by connecting
	modules together and selecting various options.&nbsp; These
	modules will have been designed by our architects
	and structural engineers.&nbsp; Using a modular system
	like this provides many advantages, the main one
	being cost savings (mainly architects' and
	draftspersons' wages).</p>
<p>This
		&quot;house designer&quot; component of our software
	system will provide tips and guidelines for designing
		a good sustainable home, and a quote for the
	cost of the building is generated in real time
	as the user develops their design.&nbsp; If the home-buyers
	want variations from the designs producible by
	the software, then an architectural
	service would be available (for an extra
	fee).</p>
<p>Once they have designed their
		home, the software will automatically generate
	architectural drawings, and parts lists which are
	sent to various suppliers.</p>
<p>The website also integrates with a mortgage broker's
	services, so that the buyer can organise finance
	for their purchase.&nbsp; Tools are provided
	to calculate loan repayments, stamp duty, legal
	fees, capital gains, depreciation and so on.&nbsp; Every
	part of the purchasing process is streamlined,
	using software tools to simplify decision-making
	and design, and the internet to keep all stakeholders
	connected.</p>
<p>&nbsp;</p>
<h2>6.&nbsp; Landscaping, Gardening and Construction</h2>
<p> This is the fun bit - where we actually build the
	ecovillage.&nbsp; This
	is, of course, a long process which will ideally
	begin soon after the land is acquired, and involves
	civil works, land rehabilitation,
	permaculture gardening, landscaping, infrastructure
	installation, and construction of the homes and
	community buildings.&nbsp; It will continue beyond
	the point where people have started moving in.</p>
<p>By this stage we will have produced a comprehensive
	set of maps, drawings, cost estimates, parts lists,
	3d models, financial projections and so on, which
	means communications with builders and other major
	actors in the construction process will be much
	more streamlined than usual.&nbsp; All of these items
	will be added to the ecovillage project website.</p>
<p>&nbsp;</p>
<h2>7.&nbsp; Ecovillage Management </h2>
<p>Once the ecovillage is &quot;operational&quot; and has actual
	humans living in it, the web-based software system
	provides a range of other functions and features
	for the residents.</p>
<p>Each of our ecovillages will have an information
	system that extends right across the site.&nbsp; This
	will include a computer built into every house,
	which provides wireless networking and internet,
	and also records information about the household's
	energy and water production and consumption.&nbsp; Antennas
	on each house and around the site will connect
	all the homes wirelessly to the community server
	(located in the IT centre), which provides the
	community's intranet, database and
	internet connection.</p>
<p>As food is grown and distributed to households in
	the ecovillage, this information is recorded, and
	people can also make requests for increase in quantities
	of certain crops.&nbsp; This data is used by permaculturists
	to determine the best way to manage the ecovillage's
	food production.&nbsp; The software, which provides
	an interactive map showing the permaculture zones,
	will help the farmers to decide where to make changes
	to their gardens.&nbsp; Data about the ecovillage's
	food production and consumption habits are also
	stored in the community database.</p>
<p>Now, when people in stage 1 of this process are
	planning their ecovillages, they will be able to look
	at the public pages for the existing ecovillage projects
	and see how much energy, water and food that they're
	producing and consuming.&nbsp; In this way they can
	see who has designed their ecovillage most effectively,
	and they can network with those people.</p>
<p>Flora and fauna surveys across the ecovillage site
	will be conducted every 3 months, to assess the
	effectiveness of rehabilitation work, and the impact
	of the human settlers.&nbsp; This data, too, will
	go into the community database. </p>
<p>As designers we will use all this data to iteratively
	fine-tune our designs and systems.</p>
<p>Other features provided by the ecovillage management
	component of the software system will include tools
	for landlords and property managers, as some
	of the homes will be owned by investors and rented
	out.&nbsp; Our website will display all the homes
	that are available to rent within our ecovillage
	network, and their rates, features and availability.</p>
<p>There will also be tools for making payments through
	the website, including mortgage repayments, rent
	payments, body corporate fees and rates.</p>
