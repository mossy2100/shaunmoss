<h1>Player Experience Overview</h1>

<p class='important'>NOTE: This is the most important part of the whole game design!</p>

<br />

<h2>Getting Started</h2>

<p>A Player can download SolSys to their computer free of charge.  The program automatically detects their JRE, Flash and browser versions, and updates these if necessary.</p>

<h3>Creating a Character</h3>

<p>The Player begins by creating character.  This involves selecting a type (Human, Android or ET) and subtype (see <a href='Types.php'>Types and Subtypes</a>), which gives them a base model avatar.  They can then customise this avatar by selecting colours for hair, skin, eyes, and clothes (all colours are available - genetic engineering in the future has enabled extensive "personal physical styling"), or by varying the body shape, adding facial or body hair, etc.</p>


<h3>Being Born</h3>

<p>Where you are "born" (i.e. where your avatar first appears) is up to you.  You can choose from any Earth village, city, moonbase, marsbase or space colony where there's a spaceport.  It doesn't matter what type or subtype you are.  Even if you're an Earthling, you can choose to appear on Mars, and so on.  It's up to the Players to create their own back story (if they want to) as to how they came to be in that location.</p>

<br />

<h2>First Experiences with SolSys</h2>

<h3>Exploring</h3>

<p>The Players do not need any money to explore SolSys.  They can travel practically anywhere in the environment for free, as there are free ferries that travel between the spaceports all the time.  So even visitors can travel around practically anywhere in SolSys, including things like robotically-guided tours of the Moon and Mars.</p>

<p>While exploring SolSys, the Player may learn many things.  They may hear of businesses looking for partners, of interesting design or construction projects, of parties of adventurers requiring new members with certain skills, of certain events that are happening, and so on.  There are frequent announcements, and many places where notices can placed or read.</p>

<h3>Communications</h3>

<p>Players can also communicate with each other in a variety of ways.

<ol>
<li>Players can hear and talk to the other Players near them.</li>
<li>Everyone in the future has <a href='Telepathy.php'>telepathy</a> that can be used to send messages to any other Player, no matter where they are located.</li>
<li>Players can leave messages on public noticeboards (accessible within the environment and also via the web).</li>
<li>Players can also purchase (very cheaply) an email address (Character.Name@solsys.net.au), and send each other emails using either the virtual environment or their normal email program.</li>
</ol>

<br />

<h2>Obtaining and using gold</h2>

<p>Players are able to purchase items within the SolSys environment using the game currency which is simply gold.  Popular items for new Players could include:</p>

<ol>
<li>A levitation harness.</li>
<li>A spacecar or spacebike.</li>
<li>An apartment or house.</li>
<li>A block of land.</li>
<li>Some new clothes.</li>
<li>A spacesuit of some kind.</li>
</ol>

<p>Although gold can be earned through selling resources and services, for most new Players the only way to obtain gold will be to buy some.  The SolSys program allows the Player to create their own e-gold account, which is used as their in-game bank account.  This is an actual real life e-gold account, which they can add to or subtract from as desired at e-gold.com.  SolSys is able to read the balance of this account, and this amount becomes the Player's in-game money.</p>

<p>SolSys subtracts a small amount from a Player's e-gold account for every hour that they spend in the game.</p>

<p>
<i>Gold in the future is worth about 10,000 times as much as now.  So, a gram of gold which costs about A$30 in 2008 has the spending power of about A$30,000 in 2158.</i>  Transactions are generally made in micrograms (&mu;gAu), milligrams (mg), or grams (gAu).
</p>

<h3>Operating a Business</h3>

<p>Players can operate businesses within the game environment by assembling the necessary elements.  An example of a small business would be a farm.  To create a farm, the Player needs to obtain land, robots, seeds, water and possibly also soil (see <a href='Commodities.php'>Commodities</a>).  Their farm produces food which can be sold to other Players, or to the Food Distrbution Network.</p>

<p>Another example would be a martian steel factory.  The Player spends money on land, a zero-point-energy power plant, mining robots, and refining and transport equipment.  They then earn money back by selling steel.</p>

<h3>Buying and Selling</h3>

<p>Items or resources can be purchased from any other Player, no matter where they are.  Players post items for sale on the SolSys-wide "classifieds" noticeboard, accessible via the web browser; other Players complete a search form to locate the items they want to buy.  The challenge is finding what you want locally, because transport costs time and money.</p>

<h3>Sports</h3>

<p>Another way that a Player can earn e-gold is by playing sports.  There are many types of sports in SolSys, including ground or aerospace racing, flyball and other low-g sports, and many more.</p>

<h3>Missions</h3>

<p>Players can also go on missions or quests to earn gold.  These may be commisioned by the UE, or by private individuals or corporations.  They may be missions to recover lost items, to search for gold or artifacts, or more commonly, to find out information.  Players can even be spies within the game.</p>



