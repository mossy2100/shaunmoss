<h2>Basic Strategy</h2>

<p>The basic strategy for producing the game is as follows:</p>

<ol>
<li>Preliminary design</li>
<li>Implementation of "Stage 1" (a playable version with a small number of types, vehicles and worlds)</li>
<li>Ongoing development in collaboration with Players</li>
</ol>

<br>


<h2>General Principles</h2>

<p>
The general principle guiding the development of SolSys will be to make the story as realistic as possible.  The environment will enforce realistic "physical" constraints on the Players, such as gravity.  Some suspension of disbelief is required, to accept the idea of living in the Solar System, being able to travel freely to any destination, with intelligent androids and ETs everywhere, but generally speaking the environment should feel plausible.
</p>

<p>
During the evolution of the game, Players will request all manner of features and story elements.  It is up to the game developers to monitor these requests and provide new features in a way that is consistent with the environment and the story of SolSys.  The game developers are also storytellers, and directors of a huge collaborative, interactive movie.
</p>


