<h1>Existing Settlements</h1>

<p>There are three main existing Settlements within the virtual environment at the start of the game:
</p>

<ol>
<li><b>Brisbane</b> - the centre of the space industry and location of the largest spaceport on Earth</li>
<li><b>Billabong</b> - the primary moonbase and HQ of the Council of Luna, located at Mt Malapert, lunar south pole</li>
<li><b>Valhalla</b> - the primary marsbase, located on Olympus Mons</li>
</ol>

<p>Ideally we will introduce three more, high-tech Settlements early in the game story:
</p>

<ol>
<li><b>ICE</b> - the Institute of Colonisation and Exploration in Antarctica (see <a href='http://ascensiontek.com/ice'>ICE</a>)</li>
<li><b>Omega</b> - a large spherical space station in Earth orbit, approximately 40% of the distance to Luna</li>
<li><b>Neith</b> - an asteroid that has been pushed into Venus' orbit, and is being converted into a space station</li>
</ol>


<pre>
New Settlements are created by the Players as the game evolves and develops.  Players can choose where new Settlements are created, what they look like and in what direction they develop.

The most important of these will be Earth Cities.  It is expected that Players will want to design futuristic models of their home cities and introduce these into the VE.  This is also potentially a good method for gathering new Players, as Players recruit friends from their city to help them with their design.

Some potential Earth cities to design

Sydney, Melbourne, Canberra, Perth, Darwin, Adelaide, Hobart
London, Paris, Amsterdam, Berlin, Helsinki, Stockholm, Oslo, Madrid, Rome, Dublin, etc.
Moscow, Mumbai, Beijing, Seoul, Tokyo, Hong Kong
Port Moresby, Djakarta, Christchurch, Auckland
Los Angeles, Denver, Las Vegas, New York, Detroit, Chicago, Atlanta, Houston, Washington D.C., New Orleans
Toronto, Vancouver, Mexico City
Buenos Aries, Brasilia
Cairo, Casablanca, Johannesburg, Nairobi, Jerusalem

These are just a few suggestions as far as cities we have to model.  We will encourage Players to virtually ignore their city of today, but rather create an entirely new city built on similar topography, to imagine what it will be like 150 years into the future, where flying cars land on roof-tops and where cities are effectively landscaped parks.

</pre>


