<h1>Game Design - Overview</h1>

<p>SolSys is primarily a POSVE (Persistent Online Shared Virtual Environment), similar in many ways to Second Life.  However, it is also a space-sim and MMORPG (Massively Multi-player Online Role-Playing Game), including elements such as character attributes and combat that are more commonly found in games like World of Warcraft.</p>

<p>In SolSys, the Players take on the personas of beings living in the Solar System 150 years into the future.  Their character can be Human (Earthling, Moonling, Martian or Venusian), Android (variety of  base models) or Extra-Terrestrial (Zeta, Arcturian, Repterrian and more) (see <a href='Types.php'>Types and Subtypes</a>).</p>

<p>Players can:
<ol>
<li>Explore the Solar System on foot, or by ground and aerospace vehicles.</li>
<li>Become involved in the SolSys community, make friends and communicate.</li>
<li>Participate in moonbase, marsbase, space station, city, village and building design projects.</li>
<li>Design and sell items such as clothes, furniture, vehicles, robots, etc.</li>
<li>Buy an apartment.</li>
<li>Buy land and build on it, or under it.</li>
<li>Start their own business.</li>
<li>Purchase and program a wide range of robots and spaceships.</li>
<li>Play various sports, including racing (ground and space), flyball and many others.</li>
<li>Participate in the creation of planetary governments.</li>
<li>Participate in planetary engineering projects.</li>
<li>Learn about the Solar System and space colonisation.</li>
</ol>
</p>

<p>
Unlike Second Life, there is danger in the space environment, and Players can be harmed.  If a Player is not mindful of their surroundings when not on Earth, they can become fried by radiation or heat, frozen, asphyxiated, crushed, lost, injured by falling, or hurt in a fight. (see <a href='Health.php'>Health</a>)
</p>

<p>
Players can make money from playing SolSys.  Their in-game bank account is linked to a real e-gold account, which they can make deposits into or withdrawls from as desired.  Players' e-gold accounts are automatically debited according to how long they spend in the VE. (see <a href='Economics.php'>Economics</a>)
</p>

<p>
The primary objective of the SolSys Players is to <b>participate in a story about colonising the Solar System</b>.  The VE starts with just a few basic settlements - it is the early phase of Solar System colonisation.  Players actively contribute to the creation of new colonies and businesses, interplanetary trade, intrigue, espionage and planetary governments.
</p>

<p>
In some ways, SolSys is a large interactive movie, in which the Players are the actors.
</p>

