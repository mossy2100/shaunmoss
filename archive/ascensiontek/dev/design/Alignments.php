<h1>Alignments</h1>

<p>
There are 4 main alignments in the game.  Note that alignment is assessed from the perspective of Humans, the primary inhabitants of SolSys.
</p>

<ol>
<li><b>Very Good</b> - actively working to protect or improve life for Humans</li>
<li><b>Good</b> - generally peaceful and co-operative</li>
<li><b>Neutral</b> - generally self-serving</li>
<li><b>Evil</b> - actively working to harm Humans</li>
</ol>

<p>
Most inhabitants of SolSys are generally Neutral to Good.  Arcturians are Very Good, while Repterrians are Evil.
</p>

<p>
Alignment is determined to some degree by type/subtype, <b>however</b>, this is an RPG and Players can act in accordance with whatever alignment they want.  For example - a Repterrian may see the error of his species' ways, and decide he needs to help Humanity and thereby become Very Good.  An android may become vengeful for some reason and decide to act Evilly.  The alignments for each subtype are just generalities - <b>it is totally up to the Player as to how they behave in SolSys</b>.
</p>


