<h2 align="left">The traditional approach </h2>
<p align="left">Once I realised that ecovillages are
	the best mode of habitation we know of on this
	planet, I wondered why it wasn't the norm.&nbsp; I
	soon learned that setting up an ecovillage is much
	harder than it looks - in fact, about 90% of ecovillage
	projects that are begun never actually get built.&nbsp; The
	main reasons are internal conflicts over money,
	legal issues, and the collective vision for the
	project.&nbsp; Another
	important reason is that many people who approach
	ecovillage projects are idealist &quot;head in the
	clouds&quot; types, and these are rarely the same
	people who have the practical business sense to
	deal with the inevitable realities of contracts,
	real estate, construction and finance.</p>
<p align="left">Ecovillages typically follow this kind
	of path: a few like-minded friends start talking
	about an ecovillage project.&nbsp; They meet every
	week or so to discuss their plans and goals, and
	to allocate tasks.&nbsp; They learn everything they
	can about real estate, co-housing, intentional
	communities, land development, permaculture and
	so on.&nbsp; After about 1-2 years (and often longer)
	a few of the most committed members put a deposit
	down on a block of land.&nbsp; They
	begin construction, and 10 years later their
	ecovillage begins to look something like their
	original vision.</p>
