<p align="left">Up until now, ecovillages have been
something of an oddity, perhaps one step removed from
	a hippy commune.&nbsp; The type of place where you
	might find what we call in Australia &quot;ferals&quot; - a
	place where things like sports cars, bacon and
	personal hygiene are frowned upon.&nbsp; But no longer!&nbsp; Ecovillages
	are undergoing a renaissance, and are gaining the
	attention of the middle-to-upper-class home-buyer.&nbsp; As
	the market shifts its preferences towards environmental
	products and services, property developers are
	reacting accordingly, and &quot;green
	developments&quot; are appearing everywhere.</p>
<p align="left">Of course, not all so-called &quot;green
	developments&quot; are ecovillages.&nbsp; Some are
	no more than your usual over-crowded suburban white-picket-fence
	housing estates, just with low-flow shower heads
	installed.&nbsp; But
	to be fair, many new developments are
	being put together by people who genuinely care
	for the environment and want to make a difference.</p>
