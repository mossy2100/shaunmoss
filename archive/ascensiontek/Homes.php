<h2 align="left">What is an &quot;ecovillage&quot;,
	anyway?</h2>
<p align="left">An ecovillage is a housing development
	in which there is a high degree of consideration
	for the environment, sharing, co-operation
	and community interaction.&nbsp; Usually
	there is a sense of &quot;custodianship&quot; of the
	land, with a large percentage of the land (often
	100%) owned collectively by all the residents.
&nbsp;	Unlike many housing developments, only about
20-25% is allocated for housing lots, the remainder
being preserved in (or rehabilitated to) its natural
state and/or allocated for community use such as food
production and shared buildings.</p>
<p align="left"> Community
	buildings may include a meeting place, hall,
	farm buildings, workshops, school, conference
	centre, even shops.&nbsp; Permaculture is a common
	feature, with the community seeking to produce
	a large percentage of its food requirements.&nbsp; Ecovillages
	also typically produce most of their energy requirements,
	recycle their water and
	incorporate water-saving technologies.&nbsp; Building
	with recycled or local materials and recycling
	of waste is also fairly standard, and many ecovillages
	engage in arts and crafts or provision of consultancy
	services in order to generate income.&nbsp; Some ecovillages
	pool their income.&nbsp; Some ecovillages do not allow
	cars, and many do not allow dogs or cats.</p>
<p align="left">The
		general principle is to live lightly on the
	land, with as small an &quot;ecological footprint&quot; as
	possible.</p>
<p align="left">But ecovillages are not only about
	the environment - unless you include humans in
	your definition.&nbsp; Ecovillages also seek to address
	certain social issues caused by the way of living
	that many of us have become used to.&nbsp; In modern
	city life, the &quot;nuclear family&quot; has fractured
	even further, with many of us  living in
	virtual isolation, a situation that often leads
	to emotional distress and even depression.&nbsp; The
	residents of an ecovillage, on the other hand,
	essentially become part of an extended family.&nbsp; They
	have shared activities and responsibilities, such
	as construction projects or taking care of animals
	or gardens, which keeps people interacting with
	each other.&nbsp; They
	also have regular &quot;village meetings&quot; in
	which decisions about the community are made by
	all the residents.&nbsp; Feelings
	of helplessness and isolation are rare in this
	kind of environment, where there
	is always someone to chat with or play
	with, and everyone has a say in how their immediate
	environment is evolving.</p>
<p align="left">So, ecovillages help Earth, and they
	help us -  they are quite simply the absolute
	best way we currently know of to live on Earth
	in a way that is sustainable and conducive to positive
	human development.</p>
