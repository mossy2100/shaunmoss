<h1>Ecovillage Design</h1>
<p>&nbsp;</p>
<h2>Summary</h2>
<p>Here's a simple summary of the main design parameters
	of our ecovillages.&nbsp; Further discussion is provided
	below. </p>
<ul>
	<li>Populations from 50 upwards (average around 500).</li>
	<li>Produce more than enough energy, using about half
	solar and half wind. </li>
	<li>Produce more than enough water, using rainwater
    	capture and recycling. </li>
    <li>Produce more than enough food, using permaculture,
    	aquaculture and poultry farming. </li>
    <li>Site-wide wireless networking and internet,
    	including computers in every building. </li>
    <li>Modern steel-frame homes that
    	employ passive heating and cooling.</li>
    <li>Energy-efficient and water-efficient appliances
    	and fixtures. </li>
    <li>Built-in support for electric vehicles.</li>
    <li>A range of community buildings and facilities.</li>
    <li>Optionally also a conference centre, school
    	or research facilities.</li>
    <li>Houses organised into clusters or hamlets,
    	and separated by greenways and wildlife corridors.</li>
    <li>Located as close as possible to population
    	centres and work availability.</li>
    <li>Use previously-cleared land for ecovillage
    	sites, and make land rehabilitation part of
    	the development. </li>
    <li>More than half total land area allocated for
    	native plants and wildlife.</li>
    <li>Minimize civil works (earthworks) as much as
    	possible.</li>
    <li>Minimize use of cement and concrete as much
    	as possible.</li>
</ul>
<p>&nbsp;</p>
<h2>Style</h2>
<p>Our ecovillages, and the buildings in them, will
	be designed in a certain way that makes them different
	from all others.&nbsp; They will be higher-tech than
	most existing ecovillages, with built-in wireless
	networking and information systems, modern computerised
	appliances and a unique style.&nbsp; The buildings
	will not be wooden but solid steel, designed for
	strength and long life.</p>
<p>However, they will not be &quot;pretty&quot;, designed for
	the lazy oh-how-about-a-nice-croissant-after-my-morning-swim
	upper-middle-class Aussie!&nbsp; These ecovillages
	are designed to be tough, functional and efficient.&nbsp; They
	will be fun places, but also a place
	where you can get your work done.</p>
<p>The homes will look great, but in a different
	way.&nbsp; They will be strong and environmentally
	friendly, in preference to shiny; surrounded by
	trees and vegetable gardens, rather than neatly
	manicured lawns with carefully placed square paving
	stones.&nbsp; They will combine the best technology
	in Australia with the toughness of the outback.</p>
<p>Our ecovillages will have a real 21st century feel
	about them.&nbsp; They will say, we live with the
	planet, and we have the strength and the technology
to survive anything.</p>
<p>&nbsp;</p>
<h2 align="left">Population</h2>
<p align="left">A typical ecovillage might have anywhere
	from a few dozen to a few thousand residents, although
	a commonly specified maximum population for ecovillages
	is between about 50 and 150, which has been calculated
	as the maximum social network size by the sorts
	of people who calculate things like that.&nbsp; However,
		some ecovillages anticipate thousands of residents,
	as in the case of the
		<a href="http://directory.ic.org/records/?action=view&page=view&record_id=199" target="_blank">Cerro
		Gordo ecovillage</a>, which aims for a maximum
		population of 2,500.&nbsp; Personally, I think 150
		people is too small for an ecovillage.&nbsp; My residential
		college at university had more people living
		in it than that.</p>
<p align="left"><a href="<?= $SilvergreenUrl ?>">Silvergreen</a> will
	only have a small population of around 50, basically
	because (a) there aren't many large blocks of land
	near Brisbane, and (b) there's less risk starting
	with a smaller project.&nbsp; Our second ecovillage,
	on the Sunshine Coast, will support from 250-500
	people.
&nbsp;	Successive projects will support larger
populations, from hundreds to thousands.</p>
<p align="left">While the reasons for setting upper
	limits on ecovillage populations are valid, the
	fact remains that if we are to live sustainably
	on Earth, we need to be able to design town- and
	city-sized sustainable communities.</p>
<p align="left">It is common within an ecovillage
	to cluster the homes into <em>hamlets, </em>which
	are separated
	by &quot;greenways&quot; -
	patches and belts of land allocated
	for permaculture or native flora.&nbsp; By expanding
	this pattern to the next scale, an &quot;eco-town&quot; can
	be formed from clusters
	of ecovillages separated
	by wildlife corridors.</p>
<p align="left">In this way, people
	will still have their social network within their
	own eco-village (which in this scenario is analogous
	to a  suburb), yet 100m or so away, there
	will be another ecovillage with different friends
	and opportunities.&nbsp; The
	most important thing is to avoid urban sprawl,
	maintaining that ratio of at least twice as much
	shared land as private.</p>
<p align="left">&nbsp;</p>
<h2 align="left">Energy</h2>
<p align="left">Electricity in an ecovillage is usually
	produced using solar, although wind turbines are
	becoming popular too.&nbsp; In our ecovillages I estimate
	using about 50-50 solar and wind.&nbsp; Solar and
	wind tend to work well together because it's often
	windy at times when it isn't sunny, i.e. at night
	or when it's overcast.</p>
<p align="left">Solar panels will be deployed on rooftops
	of buildings.&nbsp; Wind energy will be harvested
	by two or more large turbines positioned at the
	windiest locations on the site.&nbsp; While sufficient
	energy could be obtained from a single large turbine,
	having a few smaller ones will provide
	some redundancy - insurance in case one breaks.</p>
<p align="left">To avoid the need for energy storage,
	i.e. batteries, our ecovillages will be grid-connected
	wherever possible.&nbsp; This is achieved using an
	inverter, and it means that whenever the ecovillage
	is producing more electricity than it needs, the
	excess is returned to the grid, and whenever it
	is producing less than it needs, electricity is
	drawn from the grid.&nbsp; If we produce more than
	we use, we will actually get paid by the electricity
	provider.</p>
<p align="left">&nbsp;</p>
<h2 align="left">Water</h2>
<h3 align="left">Rainwater harvesting </h3>
<p align="left">Rainwater is captured in the usual
	way, using roofs, gutters, downpipes and water
	tanks.&nbsp; Because our homes are being built
	from scratch, we have the luxury of being able
	to bury the tanks or otherwise position the tanks
	beneath the floor.&nbsp; To avoid the use of cement,
	we will probably use steel or polyethylene tanks.&nbsp; Note,
	these will be large tanks, with capacity for up
	to 3 months without rain. </p>
<p align="left">The rainwater will be filtered before
	going into the water tanks, and again at kitchen
	taps.</p>
<h3 align="left">Wastewater and Recycling</h3>
<p align="left">Wastewater is recycled on-site
	as much as possible, with grey water recyclers
	will be built into every house.&nbsp; Present technology
	enables us to recycle grey water  (i.e. water
	from everywhere except the toilet, which is called
	black water) up to class AAA
	standard, which is not potable (drinkable) quality,
	but suitable for use on the garden or to wash clothes.</p>
<p align="left">Because of drought, the permaculture
	gardens will require an irrigation system that
	will probably be fed from dams.&nbsp; To avoid shortages
	what we might do is to feed excess recycled grey
	water into the dams for storage, which will provide
	enough water for the crops all year round.</p>
<p align="left">By using dry composting toilets, we
	reduce water usage, and also avoid having to treat
	or dispose of black water.&nbsp; Water is also saved
	by using low-flow shower roses, water pressure
	limiting devices, and water-efficient appliances
	such as washing machines. </p>
<h3 align="left">Hot and Cold Water</h3>
<p align="left">Energy savings in water heating are
	obtained by using either a solar hot water systems,
	or a heat pump system.&nbsp; An energy-efficient hot
	water system is now a state government requirement.</p>
<p align="left">There will also be an under-sink
		water cooler in each kitchen, to provide a
	beautiful glass of pure, cold water after a hot
	day working in the garden!</p>
<p align="left">&nbsp;</p>
<h2 align="left">Food</h2>
<p align="left">Ideally, a large fraction of the food
	supply for an ecovillage will be supplied on-site,
	using a combination of:</p>
<ul>
	<li>Permaculture gardens, for herbs, fruits, vegetables,
		nuts and grains.</li>
    <li>Poultry farming, for eggs and chicken meat.</li>
    <li>Aquaculture, for fish.</li>
</ul>
<p>On larger ecovillages there may be the option of
	having sheep, cattle or goats, to provide meat
	and milk.&nbsp; Personally I would prefer
	to avoid these, since I don't eat this kind of
	food, and also it's a terribly inefficient use
	of land and water (not very &quot;ecovillagey&quot;, if you
	get my meaning).&nbsp; However,
	it's understandable and expected that a lot of
	people will want this kind of food, and the idea
	is to make it easy for people to live in ecovillages,
	not hard.</p>
<p>The first stage of the food production system will
	be the permaculture garden and chookhouse, which
	are reasonably simple.&nbsp; The second stage will
	be an aquaponics system, which is a combination
	of hydroponics and aquaculture (fish farming).</p>
<p>Large freezers will be available 
	for food storage, built into the commercial kitchen
	which forms part of the community centre.</p>
<p>&nbsp;</p>
<h2>Information System</h2>
<h3>Networking and Internet </h3>
<p>Wireless networking will be available across the
	whole ecovillage.&nbsp; The community centre will
	house the community servers, which will be connected
	to the internet, either by cable, or satellite
	if in the outback.&nbsp; The community servers will
	also provide the community database, ecovillage
	management software (intranet), email server and
	file storage.</p>
<p>Every house in the ecovillage will have an antenna
	on the roof to connect with the community centre,
	and a wireless access point inside the house to
	provide wireless internet access.&nbsp; There will
	be no need for network points or cabling.&nbsp; Residents
	of the ecovillage will require wireless technology
	in their laptops and desktop computers, but this
	is inexpensive and is becoming standard anyway.</p>
<h3>Data collection </h3>
<p>A second-generation information system will incorporate
	data-collection devices to record the energy
	and water usage of each house.&nbsp; This will require
	various gauges and sensors that will feed information
	back to the community database.&nbsp; The data will
	provide feedback about how much water and energy
	the ecovillage is really using, which will help
	with design.</p>
<p>&nbsp;</p>
<h2>Passive Heating and Cooling</h2>
<p>Where practical, buildings will be situated with
	a northerly aspect (that is, for ecovillages in
	the southern hemisphere) in order to take advantage
	of passive solar heating to lower energy
	usage.&nbsp; The
	idea is that areas of the house where people spend
	the most time, such as the kitchen, living
	room or home office, are placed at the front of
	the house (the north) where they are warmed during
	the day.&nbsp; Areas
	where people spend less time, like bedrooms, bathrooms
	and laundry, are placed at the back of the house
	(the south) which receives less sun.</p>
<p>The homes will be pole homes, which achieves two
	things.&nbsp; Firstly, it permits passive cooling
	via air-flow underneath the floor.&nbsp; Secondly,
	it eliminates the need for a slab, which reduces
	cement usage on the site.&nbsp; (Thirdly, it provides
	a shady spot for the dog to hang out on a
	hot day :)</p>
<p>&nbsp;</p>
<h2>Environmentally Friendly Materials</h2>
<p> As mentioned, the homes will be made from steel.&nbsp; Refer
	to the article <a href="articles/WhySteel.php">Why
	Steel?</a> for more information. </p>
<p align="left">Usage of cement and concrete will be
	minimised, because cement production is one of
	the world's major contributor to carbon emissions.&nbsp; It
	is difficult to completely eliminate cement, because
	nothing compares with it for footings and foundations.&nbsp; However,
	by building pole homes we avoid the need for slabs,
	which greatly reduces cement requirements.</p>
<p align="left">Where wood is used, such as for floors
	and wall paneling, it will be ecologically certified
	or recycled wood.</p>
<p align="left">&nbsp;</p>
<h2 align="left">Recycling</h2>
<p align="left">Within our ecovillages, recycling
	will be maximised beyond what is usually required
	by council:</p>
<ul>
	<li>All paper used on the site will ideally be
		recycled or hemp-based paper, and all paper
		and cardboard will be recycled.</li>
    <li>Tins and bottles
		will be recycled.</li>
    <li>All scrap metal will be collected
		and sold for recycling.</li>
    <li>Wood will be cleaned
		up and stored for re-use.</li>
    <li>Food scraps will
		be used for chook food or composting.</li>
    <li>Computer equipment and electronic devices will
    	be recycled or given to charity where possible.</li>
</ul>
<p>The community centre will include a recycling centre
	where items can be organised for re-use.&nbsp; Each
	house will have a set of bins that are accessible
	from the kitchen, and also from outside the house
	where  they can be easily removed for transport
	to the recycling centre.</p>
<p>&nbsp;</p>
<h2 align="left">Energy and Water-Efficient Appliances
	and Fixtures </h2>
<p align="left">These will include:</p>
<ul>
	<li>Energy-efficient fridges/freezers.</li>
    <li>Energy and water-efficient washing and drying
    	machines.</li>
    <li>Low-flow shower roses.</li>
    <li>Water-pressure limiting devices.</li>
    <li>Low-energy fluorescent lighting.</li>
    <li>   Dry composting toilets.</li>
</ul>
<p>&nbsp;</p>
<h2>Support for Electric Vehicles</h2>
<p>This feature may not be needed for a couple more
	years yet, but as fully-electric vehicles become
	the norm, each ecovillage will have its own charging
	station where people can charge their cars up with
	pure renewable energy.</p>
<p>&nbsp;</p>
<h2>Community Facilities</h2>
<p>These may include: </p>
<ul>
	<li>a meeting/dining hall with tables, chairs,
		whiteboard and and projector screen</li>
	<li>office</li>
	<li>commercial kitchen with large freezers for community
		food storage </li>
	<li>caf&eacute;/bakery</li>
	<li>farm buildings</li>
	<li>workshops</li>
	<li>IT centre</li>
	<li>recycling centre</li>
	<li>childcare centre</li>
	<li>playground and sports field</li>
    <li>gym</li>
    <li>stables and pony trails</li>
    <li>recreation room with ping-pong, pool and pinball</li>
    <li>small cinema room with projector and beanbags</li>
</ul>
<p>Many of these items maybe combined.&nbsp; Smaller
	ecovillages will not be able to include all of
	these facilities, whereas larger ones will have
	all these and more.</p>
