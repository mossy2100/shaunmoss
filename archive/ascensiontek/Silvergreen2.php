<h1>Silvergreen Ecovillage</h1>
<h2>Update 16-Jun-2007 </h2>
<p>I am now thinking of building the
	first ecovillage in the Samford Valley area, rather
	than the Sunshine Coast.&nbsp; I have been tossing
	up between these two areas for some time - Samford
	is much closer to the city and therefore much more
	convenient.&nbsp; The issue is water supply.&nbsp; The
	Sunshine Coast is renown for having an abundance
	of rainfall and never having water restrictions,
	whereas the large dam near Samford is extremely
	low.</p>
<p>The convenience factor is winning out.&nbsp; The
	lack of rainfall will be a challenge, but perhaps
	one that can work in our favour - at least, it
	does justify the usage of water tanks and an advanced
	water-recycling system.&nbsp; I would
	have included such a system anyway - now it is
	absolutely necessary.</p>
<p>&nbsp;</p>
<h3>Land Availability</h3>
<p>There is not much land of any suitable size around
	Brisbane.&nbsp; Even
	around Samford itself, it is almost all gone, although
	there are parcels available further north, around
	Cashmere, Dayboro, and Draper.&nbsp; I have found
	several blocks available, all around 40-50
	acres (16-20 ha).&nbsp; The ones that are subdividable
	tend to be dearer, e.g. $1.2M.&nbsp; But there are
	a number of blocks which are not subdividable that
	only cost about $500k.</p>
<p>This raises a question of whether to subdivide,
	or to have a group title.&nbsp; I am leaning towards
	a group title, even though this is probably not
	the norm.&nbsp; It seems neater in my opinion,
	and reduces division between the residents - although
	some residents may have larger houses than others,
	all will own an equal share of the property.&nbsp; Plus
	(an important consideration) - it makes the land
	cheaper.</p>
<p>&nbsp;</p>
<h3>Buildings</h3>
<p>If a block of about 40-50 acres is used,
	then I estimate about 20-25 houses, i.e. 2 acres
	(8000m<sup>2</sup>) per house.&nbsp; For a development
	of this size, I would not expect the community
	buildings to excessively elaborate.&nbsp; Perhaps
	just the following:</p>
<ul>
	<li>A community building/hall/meeting place, which
		would include a kitchen and dining area that
		could be used for parties and village meetings.</li>
    <li>Farm building where a tractor might be stored.&nbsp; This
    	building could also double as a maintenance
    	workshop and/or recycling centre.</li>
    <li>An office.&nbsp; In the beginning this would
    	be used as a sales office, then by the
    	site manager.&nbsp; The
    	village server (computer with internet connection)
    	would be kept here.</li>
</ul>
<p>Since the office could be part of the community
	building, the site may only need two well-designed
	community buildings.&nbsp; If the villagers wish to
	use hydroponics, aquaculture or poultry farming,
	then these may require extra structures.</p>
<p>&nbsp;</p>
<h3>House description</h3>
<p>Queensland government regulations require the following:</p>
<ul>
	<li>heat pump or solar hot water system</li>
    <li>energy-efficient (fluro or compact fluro) lighting</li>
    <li>AAA-rated shower roses</li>
    <li>dual-flush toilets</li>
    <li>water-pressure limiting devices</li>
</ul>
<p>Our houses will fulfill all these requirements,
	except that they will have dry composting toilets
	instead of dual-flush.</p>
<p>They will also include:</p>
<ul>
	<li>energy-saving insulation </li>
    <li>grid-connected solar power system (incl. inverter)
		capable of producing about 2.5kW per year</li>
    <li>water tanks</li>
    <li>grey water recycler</li>
    <li>water filters</li>
    <li> energy and water-efficient appliances (e.g.
    	washing machines, fridges)</li>
    <li>antenna and wireless router</li>
</ul>
<p>&nbsp;</p>
