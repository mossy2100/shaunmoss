<h2 align="left">Ecovillages Everywhere </h2>
<p align="left">The problem with the &quot;traditional&quot;
	approach to ecovillages is that the obstacles are
	too large for most people.&nbsp; If you asked around,
	chances are a lot of people would think that living
	in an ecovillage is a nice idea.&nbsp; But is there
	one in the area they want to live?&nbsp; Probably
	not.&nbsp; Could
	they put one together?&nbsp; Who has the time?&nbsp; Secondly,
	who has the cash?&nbsp; Thirdly
	- how?</p>
<p align="left">What AscensionTek will do is make
	ecovillages into a consumer product; to remove
	all those obstacles that prevent Mr. Average Home
	Buyer from reaping the benefits of living in an
	ecovillage.</p>
<p align="left">It's
	like this - we need to be living sustainably, in
	a way that is harmonious with planet Earth, if
	indeed we intend to keep living here at all.&nbsp; Until
	Mars is terraformed we need this planet, so we'd
	better look after it.&nbsp; Also, we need to start
	living &quot;with
	each other&quot; again.&nbsp; When we know how to
	live in a community, with good, friendly relationships
	with our neighbours, that pattern will reflect
	outwards into the world.&nbsp; &quot;Ecovillages Everywhere&quot;
	means &quot;World Peace&quot;.</p>
<p align="left">When someone looks for
	a home, at the moment they can only choose from
	a range of houses, each of
	which is located on its own separate block of land.&nbsp; In
	the future, the choice might be which ecovillage
	to live in.&nbsp; In the interim, what we propose
	to do is offer products that compete with the norm,
	so the choice will look something like this:</p>
<blockquote>
<p><strong>Option A.</strong>&nbsp; Do
	I buy an ordinary project house
	built with cheap, non-recyclable
	materials on a small block of land in a suburban
	development, where I will probably rarely interact
	with my neighbours, and where I will pay a premium
	rate for my electricity, water and imported fruit
	and veg...</p>
<p align="left"><strong>Option B.</strong><em>&nbsp; Or</em> do
	I spend a little extra for a sustainable house
	in an ecovillage, where:</p>
<ul>
	<li>my electricity won't cost anything</li>
    <li>my water won't cost anything</li>
    <li>my fruit and veg will be fresh (albeit seasonal)</li>
    <li>I will know my neighbours and be involved in
    	decisions about the community</li>
    <li>my kids will be safe and have plenty of space
    	to play</li>
    <li>I will own my own house  as well as a share
    	in a large parcel of land</li>
    <li>my wife and I will have instant friends</li>
    <li>I will have access to community facilities
    	like a workshop, office, gym and games room </li>
    <li>I will be contributing to the health of my
    	planet - and myself</li>
    </ul>
<p><strong>Option C.</strong>&nbsp; <em>Or </em>(if
	there's no ecovillages in my area) do I compromise,
	and buy a solid, built-to-last steel-frame home
	for my own block of land, with built-in technology
	such as:</p>
<ul>
	<li> water-recycling
				and solar hot water equipment</li>
    <li>solar panels and maybe even a wind turbine</li>
    <li>energy-efficient appliances</li>
    <li> a permaculture or native plant
    	garden</li>
    <li>wireless internet
    	and networking all through the house</li>
</ul>
</blockquote>
<p align="left">At present, option B doesn't
	exist.&nbsp; But
	soon - it will!&nbsp; Option C exists for the home
	buyer motivated enough to create it for himself
	- but it is not a well-known option, and there are
	few (if any) providers of a complete, turn-key solution
	for a sustainable smart house.</p>
<p align="left">The first step for the company will
	be providing option B, creating and managing ecovillage
	projects.&nbsp; Once
	our own building company, <a href="gsh/index.php">Green Steel
	Homes</a>, is started, we will also
	be able to provide option C.</p>
<p align="left">As
	developers of ecovillages we expect to make good
	profits, because the value of ecovillage land rises
	 faster than the average because of their uniqueness,
	marketability, high land component, and many other
	benefits.&nbsp; We
	will promote our successes to other developers,
	so that hopefully ecovillage developments will
	become the rule rather than the exception.&nbsp; In
	this way, we begin to create &quot;Ecovillages Everywhere&quot;,
	an important step towards a happy, healthy Earth. </p>
<p align="left">&nbsp;</p>
<h2 align="left">Our Service </h2>
<p align="left">Whereas a traditional ecovillage group
	would source their own land and construct their
	own buildings, we will do all of that for our future
	residents.&nbsp; In
	this way, our ecovillage projects will be managed
	more like a traditional housing development.</p>
<p align="left">Once we have an established interest
	group for an ecovillage, and we believe that it
	will make good business sense to build an ecovillage
	in a certain area, we will find a suitable block
	of land and take care of all the development applications
	and most of the design.&nbsp; Residents can design
	the site layout online, and can also design their
	homes online, or with the help of our architect.&nbsp; Community
	buildings are designed in consultation with the
	residents, and are constructed along with other
	shared infrastructure such as the site electricity,
	water and information systems.</p>
<p align="left">Residents (especially early residents)
	will have the enjoyment of participating in a design
	project that will benefit them, without requiring
	detailed knowledge of real estate, property development,
	construction, permaculture, financing, etc.&nbsp; We
	will provide that knowledge for them.&nbsp; All they
	will have to do is participate in design of the
	community, select their house location, and design
	their house (something which is made very easy
	and cheap by using a modular system).</p>
