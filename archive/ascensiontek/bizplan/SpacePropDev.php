<h1>Phase 3: Space Property Development</h1>
<p>&nbsp;</p>
<h2>Property Development on the Moon</h2>
<p>This phase of the business will focus on the Moon,
	with the major project being the establishment
	of	<a href="../moonbase.php">Billabong</a>, an Australia-led
	lunar settlement located at Mount Malapert, lunar
	south pole.&nbsp; The settlement will ultimately
	include: </p>
<ul>
	<li> a hotel/resort</li>
	<li>permanent and holiday accommodation</li>
	<li>mines that produce water, oxygen, steel and titanium</li>
	<li>systems for producing its own electricity and
		food</li>
	<li>a scientific research facility and observatory </li>
    <li>a spaceport </li>
</ul>
<p>The base will essentially be a combination of holiday
	resort, ecovillage, mining town, and scientific
	research station on the Moon.&nbsp; Further
	development of lunar property will grow from this
	initial base.</p>
<p>&nbsp;</p>
<h2>Property Development on Mars</h2>
<p>Our Mars development plans are more ambitious than
	our lunar development plans, because the potential
	for Mars is much greater.&nbsp; Hopefully we will
	be able to begin development work around 2055-2060,
	with a colony at Olympus Mons called Valhalla.</p>
<p>There are two main reasons for selecting Olympus
	Mons for our base.&nbsp; One is the view.&nbsp; The
	view from the summit of Olympus Mons will be so
	mind-bogglingly spectacular that it will become
	the most highly-valued property on Mars almost
	as soon as colonisation begins.&nbsp; We will get
	in early and thereby obtain some of the best real
	estate in the Solar System at the cheapest possible
	price.</p>
<p>The second reason is for terraforming research.&nbsp; Mars
	is fairly big.&nbsp; Its diameter may only
	be half that of Earth's, but when it comes to gardening
	projects, terraforming Mars is right up there.&nbsp; In
	fact, it will be the most ambitious project
	ever conceived since Qin Shi Huang, the Emperor
	of China, said &quot;Maybe we should build a wall...&quot;</p>
<p>So it will
		be well worthwhile to develop our terraforming
		technologies on a small scale, such as inside
	craters - or inside the caldera of an old volcano.</p>
<p>Terraforming Mars is the ultimate goal of AscensionTek,
	and I can only hope that the company will survive
	that long (because I'm pretty sure I won't!).&nbsp; See
	the <a href="../downloads/index.php">Downloads</a> page
	for my paper &quot;Terraforming Mars&quot;.</p>
<p>&nbsp;</p>
<h2>Primary Departments</h2>
<p>These are the following main departments that will
	exist within our space property development division:</p>
<ul>
	<li><strong>Construction:</strong> Above-ground and underground base
		design and construction.</li>
    <li><strong>Resources:</strong> In-situ resource utilisation, which
    	includes mining (and atmospheric mining) and
    	production of air, water, metals and other
    	materials.</li>
    <li><strong>Energy:</strong> In-situ production
    	of clean energy, especially solar, wind and
    	zero-point energy systems. </li>
    <li><strong>Biological systems:</strong>  Human
    	communities, space agriculture, biotechnology
    	and terraforming.</li>
</ul>
<p>More about these later.</p>
