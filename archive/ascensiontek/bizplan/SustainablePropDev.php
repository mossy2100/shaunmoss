<h1>Phase 1: Sustainable Property Development</h1>
<p>While the overall vision for the business spans
	a century or more, our primary
	focus for approximately the next 30 years is
property development on Earth.</p>
<p>Our Earth development plans are preparation
	for more ambitious space development.&nbsp; This
	helps to put things in perspective.&nbsp; Earth-based
	projects that initially seem daunting appear rather
	easy when compared with, for example, constructing
a marsbase.</p>
<h2>An independent business </h2>
<p>However, this phase of the business does represent
	a complete business strategy on its own, which
	increases viability
	of the company, and insures against the
	possibility that space colonisation may not start
	until further in the future than we imagine.</p>
<p>Having
		a profitable, growing, Earth-based business
	means that the founders and other stakeholders will
	not run out of steam waiting for their space dreams
		to manifest, nor will they need to pin their
	hopes for a return on their investment on indefinable
		future events.</p>
<h2>Primary function </h2>
<p>This component of the business exists for several
	reasons:</p>
<ol>
	<li> Accumulating money and material assets.</li>
	<li>Creating a positive benefit for Earth.</li>
	<li>Establishing a reputation for AscensionTek as
		a positive, creative and
		productive organisation.</li>
	<li>Gaining experience in developing highly independent,
		high-tech human settlements.</li>
</ol>
<h2>Utilising lessons learned </h2>
<p>During this Earth-development phase a percentage
		of time and resources will be allocated to
	examining how our experience can be applied to
	space development.&nbsp; This
	will include space settlement design studies.</p>
