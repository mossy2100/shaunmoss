<h1>Phase 1: Sustainable Property Development</h1>
<h2>Concept Review</h2>
<p>I feel the need to review the concept for the business!&nbsp; I
	think I am trying to do too much off the bat.&nbsp; It
	may not be necessary to hire an architect or draftsperson
	f\or the immediate role - but then again, it might
	still be useful.</p>
<p>The most important part of my business in the beginning
	will be building the website to create ecovillage
	projects.</p>
<p>Okay, so people come to the website and create their
	ecovillage projects.&nbsp; The company then provides
	a range or organisational services to help these
	projects come to life.&nbsp; The problem is that ecovillages
	are very challenging things to create.&nbsp; So, to
	successfully inhabit this planet, we need to make
	them accessible, so that Joe Public can find a
	place to live in an ecovillage without having to
	learn too much or change too much.&nbsp; It's all
	about lowering the obstacles to sustainable living.&nbsp; With
	each project we make it easier for people to purchase
	homes in an ecovillage.</p>
<p>The website tells us where there are people who
	really want to live in an ecovillage.&nbsp; We can
	use this information to select locations.</p>
<p>We find appropriate land in co-operation with the
	potential residents (or they can do it).&nbsp; We
	also provide advice about what sort of land to
	use.</p>
<p>I think some groups will want to most of the planning
	themselves - others will not have a clue, and we
	will do most of it for them.&nbsp; In some cases we
	will simply build the ecovillage and sell houses
	in it.</p>
