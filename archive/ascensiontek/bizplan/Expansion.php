<h1>Growth System and International Expansion</h1>

<p>The plan for AscensionTek is to expand globally,
	with locations all over the world including Antarctica.</p>
<p>These locations will include:</p>
<ul>
	<li>Head offices in Canberra, Amsterdam, Nairobi,
		Hong Kong, New York and Brasilia.</li>
    <li>Sales and IT/multimedia offices. </li>
    <li>Ecovillages and green developments.</li>
    <li>Research and Development centres.</li>
    <li>Technology display centres.</li>
    <li>Spaceports.</li>
</ul>
<p>The company is currently located in Brisbane, Queensland,
	Australia, where we plan to establish both an ecovillage
	and an office location.&nbsp; The next step will be
	to establish similar locations on the Sunshine
	and Gold Coasts and other surrounding areas.</p>
<p>Where we expand to from there will depend partly
	on the staff, because new managers will have the
	choice of where they open their location.&nbsp; We
	will encourage expansion up the Queensland coast
	as far as Cairns, establishing locations in Maryborough,
	Bundaberg, Gladstone, Rockhampton, Mackay, Townsville
	and Cairns.&nbsp; At the same time, we will also be
	looking to open branches in every capital city
	in Australia, followed by major cities in New Zealand,
	Papua New Guinea and Indonesia.</p>
<p>Our first major head office will be located in Canberra,
	with the second in Hong Kong.&nbsp; Other cities in
	Asia where we envisage establishing locations include
	Seoul, Tokyo, Beijing, Mumbai, Kathmandu, Singapore,
	Baikonur and Moscow.</p>
<p>With a Europeaon headquarters in Amsterdam,
	we will expand into Europe with locations in London,
	Paris, Berlin, Rome, Athens, Madrid, Stockholm,
	Bern and Zagreb.&nbsp; Locations
	in Africa will include Casablanca, Cairo and Durban,
	with African headquarters in Nairobi.</p>
