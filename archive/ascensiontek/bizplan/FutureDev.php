<h2>Physics Research</h2>
<p>It's hard to say if or when this aspect of the company
	will begin.</p>
<p>Let's put it this way.&nbsp; The general philosophy
	of the company is this:&nbsp; There
	are some things we need to achieve our goals.&nbsp; If
	no-one else is doing them, then we will.</p>
<h3>Levitational Propulsion Systems </h3>
<p>One of the things we need to achieve our goals is
	known as CATS, or &quot;cheap access to space&quot;.&nbsp; It
	seems the best way to achieve this is by &quot;anti-gravity&quot;,
	although I prefer the term	&quot;levitational
propulsion&quot; or simply &quot;levity&quot;.</p>
<p>Reactionless propulsion is real.&nbsp; There
	is no longer any doubt about this - just visit <a href="http://jnaudin.free.fr/lifters/main.htm" target="_blank">JLN
	Labs</a> and
	find out how you can easily make your own model
	out of balsa wood, copper wire and aluminium foil
	that defies Newton's Second &quot;Law&quot;.&nbsp; Then
	you might like to visit <a href="http://www.americanantigravity.com/" target="_blank">American
	Antigravity</a> or
	<a href="http://www.gctspace.com/main.html" target="_blank">Gravity
	Control Technologies</a>.&nbsp; Both NASA
	and ESA are researching levitational propulsion.</p>
<p>The value of a space transportation system that
	relies on a reactionless and possible even fuelless
	propulsion system is inestimable - especially to a
	space property development business!&nbsp; But what
	about the value to Earth of very fast and cheap transport
	between any locations around the world?&nbsp; If nothing
	else, consider global food distribution.
&nbsp;	Fresh fruit and veggies for everyone!&nbsp; No-one
would ever be hungry again.</p>
<p>Unfortunately,
	development in this field is hampered
	by general skepticism from the mainstream scientific
	community, a lack of willingness to accept that
	the universe is more complex than we thought,
	and a certain stigma from its connection with
	the UFO community and conspiracy theories.&nbsp; None
	of this really matters - if
	these technologies have real potential, and the
	evidence suggests that they do, then, in light
	of the potential benefits, we should be investing
	as much as possible in their rapid development.</p>
<p>Hopefully this technology will begin to be taken
	seriously and developed by others in the near term,
	so we can use them.&nbsp; Otherwise we may invest
	in them ourselves when funds are available.</p>
<h3>Zero-Point Energy </h3>
<p>This technology is very much in the same category
	as levitational propulsion, and indeed, many of
	the people researching one are also researching
	the other.&nbsp; The existence of ZPE is predicted
	by quantum physics and refers to very high levels
	of energy available everywhere in the universe,
	even in a vacuum, which is why it is also sometimes
	known as vacuum energy.</p>
<p>The value of ZPE technology is even greater than
	levitational propulsion.&nbsp; Not only will we not
	need fossil fuels or nuclear fission any more,
	but if
	ZPE power plants can be made cost-effective, then
	they will even compete with solar and wind, which
	are dependent on weather.&nbsp; But
	ZPE is always there - everywhere!</p>
