<h2>Analogue Research </h2>
<p>This is unlikely to be a directly profitable activity.&nbsp; Nonetheless,
	major benefits can be gained by investing
	in analogue research:</p>
<ol>
	<li><strong>Knowledge and experience.</strong>&nbsp; Plenty
		of valuable experience can be (and is being)
		gained from analogue space settlements on Earth,
		which provide insight into many of the engineering
		and human factors challenges we will face in
		space.&nbsp; We
		will certainly avoid many costly mistakes
		by operating analogue stations on Earth before
		building real ones.</li>
    <li><strong>Advertising.</strong>&nbsp; Analogue
    	space settlements gain attention in the media
    	and especially in the space community.&nbsp; Sponsoring
    	these projects would be good for our reputation
    	and for getting the company known.</li>
</ol>
<p>At this stage it is envisaged that AscensionTek
	will mainly support <a href="http://www.marssociety.org.au/MarsOZ.php" target="_blank">Mars-Oz</a> (the
	<a href="http://en.wikipedia.org/wiki/Mars_Analogue_Research_Station" target="_blank">Mars
	Analogue Research Station</a> being built by <a href="http://www.marssociety.org.au/" target="_blank">Mars
	Society Australia</a>),
	and possibly also analogue studies being proposed
	by the <a href="http://www.marshome.org/" target="_blank">Mars
	Foundation</a>.&nbsp; I would also like to build a
	Moon/Mars analogue space settlement in Antarctica,
	eventually working our way up to the <a href="../ice/index.php">Institute
	for Colonisation and Exploration</a>. </p>
