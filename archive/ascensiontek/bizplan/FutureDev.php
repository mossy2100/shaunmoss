<h2>Environment Control Systems</h2>
<p>An ECS (Environment Control System) controls parameters
	like the temperature, humidity and constituents (most
	importantly O<sub>2</sub> and CO<sub>2</sub> concentrations)
	of air within a volume such as a habitat or greenhouse.&nbsp; Actually,
	any air-conditioner, heater, humidifier, dehumidifier
	or air-scrubber is an ECS of some description.</p>
<p> ECS's are needed for any communities in extreme
	environments, whether in the desert, in polar regions,
	or in space.&nbsp; We will develop these types of
	systems for use in advanced ecovillages before using
	them in analogue and real space settlements.&nbsp; For
	example, when building ecovillages in the Queensland
	outback, we may like to build environment-controlled
	greenhouses so the residents can grow their own food
	locally.</p>
