<h1>Phase 2: Future Developments</h1>

<p>With a significant capital base and reliable income
	streams established through the property development
	business, the company will be in a position to
	shift into phase 2, in which we explore commercial
	opportunities with the following attributes:</p>
<ul>
	<li>Highly profitable.</li>
    <li>Interesting and fun. </li>
    <li>Creates a positive benefit for Earth and society.</li>
    <li>Provide experience and/or produce technologies
    	that will move the company towards its space
    	colonisation goals.</li>
</ul>
<p>The two most important industries that fit these
	categories are <strong>metals </strong> and <strong>robotics</strong>. </p>
<p>&nbsp;</p>
<h2>Metals</h2>
<p>There are a number of reasons for getting involved
	in metals: </p>
<ol>
	<li>After tourism, recreation and property development,
		metals production will be one of the
		primary economic drivers on the Moon and Mars.
		 Just as with
		ecovillages, being involved with metalliferous
		mining projects on Earth will be good practice
		for space.</li>
	<li>Producing our own steel will benefit our steel
		building business. </li>
	<li>Money.&nbsp; Metals prices continue to climb,
		and demand
		for metals is likely to increase massively
		during the 21st century for several reasons:
        		<ul>
        			<li>growing population</li>
        			<li>global industrialisation</li>
        			<li>building new infrastructure in the
        				wake of climate change</li>
        			<li>preference for metals in construction
        				as living trees become more
        				highly valued</li>
		</ul>
	</li>
</ol>
<p>For property and infrastructure development on the
	Moon and Mars, local production of metals and other
	materials is essential.&nbsp; Furthermore, as
	demand for metals on Earth climbs, it may become
	economically viable to import metals from the
	Moon, Mars and the asteroids once the cost of transport
	becomes low enough.&nbsp; Concentrated metals are
	available in great abundance in space, and can
be obtained without biological impact.</p>
<p>Many space colonies will be analogous
	to remote mining towns, where entire communities
	crystallise around concentrated metal deposits,
	so it will be highly valuable for
the company to have experience with metalliferous
mining, metals production, and mining communities.</p>
<h3>Iron and Steel</h3>
<p>Steel is the most important metal on Earth,
	as is likely to be the case on Luna and Mars
	as well.&nbsp; Iron, the main constituent of steel,
	is the most abundant metal in the universe, which
	makes steel very cheap.&nbsp; Steel is a great
	material for making all kinds of things from paperclips
	to transcontinental railways.</p>
<p>Producing our own steel will deliver advantages
	to the steel building component of the business,
	helping to streamline operations, ensuring reliable
	supply, and lowering costs.</p>
<p>One of the most important goals for the company
	is to develop a new method of steel production
	that does not require fossil fuels for either iron
	ore reduction or power generation.&nbsp; This
	idea developed from research into methods for manufacturing
	steel on Mars (see the <a href="../downloads/index.php">Downloads</a> page
	for my &quot;Steelmaking on Mars&quot; paper).</p>
<h3>Titanium</h3>
<p>Titanium is becoming an increasingly important and
	popular metal.&nbsp; Its excellent material
	properties make it ideal for aerospace applications,
	 which is interesting considering its abundant
	availability on the Moon.&nbsp; Titanium's
	value will be emphasised on the Moon because of
	the extremes of temperature - titanium is
	resistant to the effects of thermal
	cycling.</p>
<p>Furthermore, because titanium
	minerals absorb helium-3 particularly well, titanium
	and helium-3 are usually found in concentration
	together on the Moon.&nbsp; Helium-3
	has been identified as a highly valuable
	substance because fusion reactors
	fuelled by helium-3 do not produce any nuclear
	radiation - only water.&nbsp; Excellent
	leverage could be obtained by producing both titanium
	and helium-3 from the same lunar mines.&nbsp; (Note
	that AscensionTek does not encourage development
	of nuclear fusion as we see zero-point energy as
	having much more potential for safe, abundant energy
	production.&nbsp; However, our policy does not mean
	that a significant market for helium-3 will not
	exist!)</p>
<p>New methods
			of refining titanium such as the <a href="http://en.wikipedia.org/wiki/FFC_Cambridge" target="_blank">FFC
			Cambridge Process</a> mean that titanium
			is becoming cheaper, and will
			therefore soon find new applications and
			new markets.
		&nbsp;	By becoming involved in this industry we
		can make significant profits both on and off
		Earth.</p>
<p>&nbsp;</p>
<hr>
<p>&nbsp;</p>
<h2>Robotics</h2>
<p>With the development of a robust software and IT
	department, and experience with building smart
	(computerised) houses, the company
	will be well-positioned to develop its own artificial
intelligence and robotics department.</p>
<p>Especially once we are operating our own metalliferous
	mines, the company will directly benefit from
	developing robots and automation systems for the
	mining industry.&nbsp; We
	may also look at developing robots for construction
	and land-rehabilitation (e.g. automated greenhouses
	and tree-planting robots).&nbsp; Robotics will also
	play a critical role in space colonisation, being
	useful in mining, construction, exploration and
	transport, and it will be possible to adapt many
	of the robots developed for applications on Earth,
	for use on the Moon and Mars. </p>
<p>Robotics and AI promise to be fundamentally Earth-changing
	technologies during this century, freeing us from
	most kinds of manual labour and systemisable decision-making.</p>
<p>This
		will be an extremely profitable component of
	the business.</p>
<p>&nbsp;</p>
