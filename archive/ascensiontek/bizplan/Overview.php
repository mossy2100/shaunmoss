<h1>Long-term Overview</h1>
<p>This page discusses the
	long-term	vision
	of the company, to illustrate the scope of the
	plan, and to provide a context for the first major
	phase of the business.</p>
<p>&nbsp;</p>
<h3>Space property development?&nbsp; Are you for real?</h3>
<p>As described in the introduction, AscensionTek is
	mainly focused on property development, initially
	on Earth, progressing to the Moon in about 30 years,
	and Mars about 20 years after that.</p>
<p>This
	may seem fanciful or far-fetched.&nbsp; However, consider
	everything that has happened over the past 100
	years.&nbsp; Things will
	change much more during the next 100.</p>
<p>Space tourism has already begun, with
	the fifth space tourist returning to Earth in April
	2007.&nbsp; Research indicates that the commercial
	opportunity represented by space tourism is significant,
	and several companies are currently positioning
	themselves to create and capitalise on this new
	industry, such as <a href="http://www.virgingalactic.com/" target="_blank">Virgin
	Galactic</a> and <a href="http://www.spaceadventures.com/" target="_blank">Space
	Adventures</a>.&nbsp; Others are providing
	 services such as low-cost rocket
	launches (<a href="http://www.spacex.com/" target="_blank">Space-X</a>)
	and commercial orbital space complexes (<a href="http://www.bigelowaerospace.com/" target="_blank">Bigelow
	Aerospace</a>).</p>
<p>Once people begin visiting space and spending more
	time there, it is highly likely that some
	will want to live there, especially on the Moon
	and Mars.&nbsp; Clearly, there are already plenty
	of people who want to live in space, and many cite
	eventual colonisation as one of the most important
	reasons for space exploration. There are numerous
	good reasons for colonising space (see <a href="http://www.space.com/scienceastronomy/solarsystem/tenreasons_lead_000723.html" target="_blank">Top
	Ten Reasons to Inhabit Outer Space</a>).</p>
<p>Many plans already exist for establishing permanent
	settlements on the Moon and Mars.&nbsp; NASA are planning
	to build a moonbase by approximately 2024.&nbsp; Russia
	is planning one by 2025, Japan by 2030, and China
	by 2032.&nbsp; There
	are also plans for private moonbases (<a href="http://www.asi.org/" target="_blank">Artemis
		Society</a>) and
	marsbases (<a href="http://www.marshome.org" target="_blank">The
	Mars Homestead Project</a>).</p>
<p>NASA have stated that their role is as a pathfinder,
	conducting missions to refine and validate
	technologies and produce data, in order to lower
	the barriers for private enterprise to follow behind.&nbsp; There
	is general recognition within the space community
	that private enterprise will be the main driver
	of space colonisation, not government.</p>
<p>What
		this all amounts to is that space colonisation
	is a real, commercial process which is already
	underway.&nbsp; For
	AscensionTek to be a leader in this new industry,
	the time to begin  is now.</p>
<p>&nbsp; </p>
<h3>AscensionTek is different</h3>
<p>What makes AscensionTek different from other space
	businesses is that we do not focus only on space.&nbsp; Instead
	of spending the next 30-50 years purely on multimedia,
	analogue research or design studies, we have chosen
	instead to engage in business activities with
	real and growing markets on Earth.</p>
<p>This approach
	will enable the company to raise its own money
	instead of being entirely dependent on investment,
	will greatly reduce our risk, increase our probability
	of success, and provide us with valuable
	experience in business and property development.&nbsp; We
	will still be able to allocate a certain percentage
	of company resources to multimedia,
	analogue research and design studies, and in fact,
	the amount that we can invest in these activities
	will be greater.</p>
<p>There are many things we can do
	to prepare for  building
settlements on the Moon and Mars.&nbsp; After investigating
a range of options over the past 8 years, we have identified
the activity which offers the most advantages
- namely, <em>the
design, construction and operation of ecovillages</em>.</p>
<p>&nbsp;</p>
<h2>Schedule</h2>
<p><strong>2007: </strong>The company focuses on its
	core business, providing sustainable steel-frame housing,
	ecovillage design and construction, and consultancy
	services. We develop an advanced software system to
	manage, streamline and optimise the business. A percentage
	of time and resources (~5-10%) is channeled into moonbase
	and marsbase design studies. </p>
<p><strong>2020: </strong>In preparation for space
	development the company branches into new areas, such
	as analogue research, environment control, robotics,
	and metals production. </p>
<p><strong>2035: </strong>We begin our lunar development
	phase with our first moonbase, either independently
	or in co-operation with other partners such as space
	tourism or mining companies. </p>
<p><strong>2055: </strong>Our Mars development phase
	begins using a similar strategy, incorporating
	lessons learned from Earth and the Moon.</p>
