<h1>Introduction</h1>
<p>AscensionTek is a property and technology development
				company for Earth, the Moon and Mars.&nbsp; The
				company will primarily provide services
				such as building and community design,
				construction, in-situ resource utilisation,
				life-support, environment control,
				and other relevant technologies, to
				businesses and individuals desiring
				habitation solutions on these 3 worlds.</p>
<p>The company is founded on altruistic philosophies,
	its function being to create a large
	a positive benefit as possible for the human family
	over the course of the 21st century.&nbsp; Although
	the primary method for achieving this will be through
	the development and provision of high-tech sustainable
	habitation solutions on Earth and in space, the
	company may also take advantage of other worthwhile
	opportunities as they present themselves, such
	as production systems for food, water, energy and
	metals, healing technologies, or spreading positive
	ideas through film and television. </p>
<p>The sustainable property development component of
	the business will operate using the brand
	name <a href="../gsh/index.php">Green Steel Homes</a>.&nbsp; This
	will include the following products and services:</p>
<ol>
	<li> Design and construction of environmentally-friendly
		steel-frame homes that include built-in technology
		such as solar panels, water recyclers and
		energy-efficient appliances.</li>
    <li>Design and construction of ecovillages of all
    	sizes and in many places around Earth.</li>
    <li>Related consultancy services.</li>
</ol>
<p>The company will also actively promote
	the ecovillage concept and will generally seek
	to encourage sustainable development wherever
	and whenever practical.</p>
<p>By engaging in property development projects of
	this type on Earth, the company will gain valuable
	experience directly applicable to
	moonbase and marsbase design and construction.&nbsp; Approximately
	5-10% of company time and resources will be allocated
	to moonbase and marsbase design projects during
	the first few decades of the company, with the
	intention that from about 2035 or so we will be
	able to participate in or initiate lunar property
	development projects.</p>
<p>With
		experience gained on Earth and the Moon, we
	aim to become involved in martian property development
		from about 2055 or so.</p>
