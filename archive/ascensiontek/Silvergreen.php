<h2 align="left">Design Notes </h2>
<p align="left">Naturally, I have not purchased any
	land as yet because there are things that need
	to be done first, such as setting up a business
	premises as well as the necessary legal entities
	for the ecovillage.&nbsp; However,
	design work can still be begun.&nbsp; Most of the
	fundamental design parameters for our ecovillages
	are listed on the <a href="EcovillageDesign.php">ecovillage
design page</a>.</p>
<p align="left">Because Silvergreen is our very first
	ecovillage, there is much less room for experimentation,
	and we must seek to reduce the challenge as much
	as possible.&nbsp; That said, all these design notes
	are highly flexible, and will evolve as more people
	add their input. </p>
<p align="left">&nbsp;</p>
<h3 align="left">Location</h3>
<p align="left">Originally I had planned for Silvergreen
	to be located on the Sunshine Coast.&nbsp; Rainfall
	is very good there, and there are still large blocks
	of land available.&nbsp; However, I have decided to
	locate the first ecovillage in the Samford Valley
	region, and the second one at the Sunshine Coast.</p>
<p align="left">The Samford Valley is a very beautiful
	location featuring good-natured people, lots of
	trees and wonderful, sweet-smelling fresh air.&nbsp; It
	suits mainly because of convenience to Brisbane,
	where I am located, and where the business premises
	will be located.&nbsp; The
	largest blocks of land around there are mostly
	about 16-20 hectares (40-50 acres), which should
	be large enough for a small ecovillage.</p>
<p align="left">The challenge of the Samford Valley
	is lack of rainfall - the dams around
	there are currently very low.&nbsp; Fortunately
	our ecovillage design will include all the water
	catchment and recycling technology needed to overcome
	this challenge. </p>
<p align="left">&nbsp;</p>
<h3 align="left">Size</h3>
<p align="left">I envisage a population for Silvergreen
	of only around 40-80 people.&nbsp; Staring the
	business with a project of this scale will keep
	things simple, estimates will be more accurate,
	and it will be easier to control costs and to raise
	money.&nbsp; So, I am working on rough figures of
	about 20 homes, with an average of 3 people
	per home - a total of about 40 adults and 20
	children.</p>
<p align="left">This figure is based on a block of
	land of about 16 hectares (40 acres), which is
	the size of most of the large blocks of land in
	the area around Samford.&nbsp; This
comes to about 0.8 hectares (8000m<sup>2</sup> or 2 acres) per
	household.</p>
