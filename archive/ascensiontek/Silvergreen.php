<p align="left">As you can see, there is some considerable
	variation in size and population density, with
	an average area per household of about 1.5 ha.&nbsp; The
	last two ecovillages in the list represent the
	extremes.</p>
<p align="left">Silvergreen will therefore be on the
	higher density side (similar to the Ecovillage
	at Currumbin).&nbsp; This
	is hard to avoid on a relatively small block of
	land close to the city, because land is more expensive
	and less available.&nbsp; Even so, the residents will
	still own 8 times as much land as they would living
	on a typical 0.1 ha (&frac14; acre) suburban block.</p>
<p align="left">For the sake of a preliminary design,
	I am working with the following figures:</p>
<ul>
