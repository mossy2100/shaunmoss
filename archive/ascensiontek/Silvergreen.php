<h3 align="left">Features of the Site</h3>
<p align="left">There
	are a few other features we need to look for in
	an ideal ecovillage site.</p>
<p align="left"><strong>1.&nbsp; Proximity to civilization.</strong>&nbsp; Not
	everyone wants to live out in the sticks, and indeed,
	the whole point of this business is to bring ecovillages
	to the masses.&nbsp; The site must be a short drive
	to the nearest population centre, and ideally near
	a train station or bus stop.</p>
<p align="left"><strong>2.&nbsp; Partly or wholly cleared.</strong>&nbsp; A
	fully vegetated site won't be ideal, because we
	don't want to have to cut down any trees to make
	room for houses.&nbsp; If we select a site that
	has been partly cleared, then rehabilitation of
	the cleared land becomes part of the project.&nbsp; While
	this is an extra expense, it will increase the
	environmental value of the ecovillage - we will be
	able to promote what a positive effect the ecovillage
	has had on the land.&nbsp; Also, if
	there has been some clearing we have more flexibility
	about how to lay out the site.</p>
<p align="left"><strong>3.&nbsp; Fresh water sources.</strong>&nbsp; A
	creek, stream, river, lake or dams will really
	add something to the site, providing water for
	irrigation and for animals.</p>
<p align="left"><strong>4.&nbsp; Good soil quality.</strong>&nbsp; We
	need to be able to successfully grow a wide range
	of food on the land, so soil testing will be essential
	before purchasing a site.</p>
<p align="left"><strong>5.&nbsp; Unencumbered.</strong></p>
<p align="left"><strong>6.&nbsp; Correctly zoned and
	in harmony with council regulations.</strong> &nbsp; Although
	these obstacles can sometimes be overcome, things
	will go much easier if the land is in the correct
	zone (i.e. residential or semi-rural) and if the
	project does not conflict with local council.</p>
<p align="left">&nbsp;</p>
<h3 align="left">A Single Builder</h3>
<p align="left">This is something that we will
	be doing which is a little different.&nbsp; Because
	most ecovillages are put together by a group of
	independent like-minded people, rather than a property
	development company, the houses (if they are built
	from scratch) are often quite different.&nbsp; For
	example, at the Ecovillage at Currumbin, the residents
	can build any kind of house they like as long as
	it is within certain design parameters.</p>
<p align="left">However, there are significant
	benefits to be had from using a single builder:</p>
<ul>
	<li>The whole site can be constructed as a single,
		integrated system, making the ecovillage more
		beautiful and harmonious, and streamlining
		the integration of homes with the site's energy,
		water and information systems.</li>
    <li>Leveraging cost benefits through re-use of
    	design elements, drawings, and engineering
    	requirements.</li>
    <li>Cost benefits from bulk-buying of building
    	materials.</li>
    <li>Huge reduction in overheads that would be incurred
    	if architects had to check every
    	design.</li>
    <li>Only having to educate one builder about the
    	design goals of the ecovillage.</li>
</ul>
<p align="left">Ideally, the one
	builder will be us: Green Steel Homes.&nbsp; However,
	for this first ecovillage, this may not be practical
	since Green Steel doesn't actually exist yet.&nbsp; It
	all depends what level of investment the business
	receives and when.</p>
