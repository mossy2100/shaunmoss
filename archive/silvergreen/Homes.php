<h1>Homes</h1>
<p>The features of Silvergreen's homes include:</p>
<ul>
	<li>Strong, steel frame</li>
	<li>Long-lasting; storm, earthquake, fire and termite
		resistant. </li>
	<li>Spacious, comfortable and stylish.</li>
	<li>Built using environmentally-friendly building
		materials. </li>
	<li>Thermally efficient, designed to use passive heating
		and cooling.</li>
	<li>Incorporating energy production technology such
		as solar panels.</li>
	<li>Water tanks and water recycling system.</li>
	<li>Energy-efficient hot water and cold water systems.</li>
	<li>Energy and water-efficient appliances and fixtures.</li>
	<li>A built-in wireless router and access point, providing
		internet all the time, anywhere in the house.</li>
	<li>Integrated with the natural environment through
		outdoor spaces.</li>
    <li>Permaculture and native
			plant gardens.</li>
</ul>
<p>&nbsp;</p>
<h2>Footings</h2>
<p>Our homes will be pole homes, with steel posts set
	in concrete footings.&nbsp; This is done for two reasons
	- to reduce usage of concrete as much as possible,
	and also to induce passive cooling via air flows beneath
	the floor.</p>
<p>Cement production is one of the world's largest
	sources of carbon emissions, so we endeavour to reduce
	usage of cement and concrete as much as possible.&nbsp; However,
	there are few suitable and cost-effective alternatives
	for footings.</p>
<p>&nbsp;</p>
<h2>Steel-frame</h2>
<p>We may use the steel-frame construction
	system designed by <a href="http://www.bluescopesteel.com.au/index.cfm?objectID=2CA77BBC-0EB5-11D4-89F500C04FCF6B8F" target="_blank">Lysaght</a>,
	including their Quika-Floor, Supraframe and Supratruss
	systems.&nbsp; These comply with all Australian Standards,
	have been appraised by the CSIRO, and have received
	Certificates of National Accreditation as a building
	system.</p>
<p>Using this system saves an enormous amount of time,
	as well as the expense of engineering certification.</p>
<p>&nbsp;</p>
<h2>Cladding</h2>
<p>Exterior wall and roof cladding will probably be
	Colorbond steel from Lysaght.&nbsp; Aside from the
	environmental benefits of steel, there are a range
	of profiles and colours to choose from, and the effect
	is modern and attractive.</p>
<p>People are often concerned that using steel cladding
	will mean that the house will heat up excessively
	during summer.&nbsp; However, with an air gap between
	the external and internal cladding, very little heat
	is transferred to the inside of the house.&nbsp; This
	can be mitigated by using insulation between the external
	and internal cladding, and reduced even further (especially
	on the roof) by using a light colour.</p>
<p>For internal cladding and floors, we may use an
	environmentally-friendly product such as <a href="http://www.enviroboardcorporation.com/" target="_blank">Enviro
	Board</a>.&nbsp; This is an area of current research.</p>
<p>&nbsp;</p>
<h2>Energy Production</h2>
<p>The homes will have solar panels fitted to the roof,
	which will produce about half of the energy requirements
	of the household (the remainder of the energy will
	come from wind turbines positioned elsewhere on
	the site). </p>
<p>The roofs will  be angled at 17.5&deg; and
	facing north, in order to maximise the amount of
	solar energy captured. </p>
<p>&nbsp;</p>
<h2>Energy Efficiency</h2>
<p>The homes will be designed in accordance with new
	government energy efficiency guidelines.&nbsp; Incorporating
	passive heating and cooling design principles will
	reduce the costs of temperature control, which typically
	form a large fraction of a home's electricity expenses.</p>
<p>Insulation made from sustainable materials will
	also be used to reduce heating and cooling costs.</p>
<p>The homes will come with energy efficient lighting,
	and possible also devices to ensure that items
	such as televisions, DVD players and computer screens
	do not go to &quot;standby&quot; mode, but are completely
	turned off when not in use.&nbsp; Another
	system under consideration uses occupancy sensors
	that automatically turn lights off when there's
	no-one in a room.</p>
<p>&nbsp;</p>
<h2>Rainwater Harvesting</h2>
<p>Since we are building new homes, we have the luxury
	of locating water tanks under the house.&nbsp; These
	will be larger-than-usual tanks (around 50,000L)
	to insure against the possibility that drought
	conditions may worsen during climate shift.&nbsp; Positioning
	water tanks under the house saves space, and also
	makes it easier to connect downpipes from all sides
	of the house to the tanks, thus utilising the entire
	roof area for rain harvesting. </p>
<p>Tanks will most likely be steel or polyethylene,
	environmentally-friendly alternatives
	to concrete.&nbsp; Poly
	tanks are long-lasting, and provide better value
	in terms of dollars per litre of water storage
	than other options.&nbsp; Steel has the advantage
	of being fire-proof.</p>
<p>Gutter mesh, such as steel, fire-proof <a href="http://www.rainharvesting.com.au/blue_mountain_all_steel_gutter_mesh.asp" target="_blank">Blue
		Mountain Mesh</a>, will be utilised to ensure that
		leaves and debris do not clog the gutters, and rain
		heads will be fitted to downpipes to deflect leaves
		and other debris away from the flow of water.&nbsp; Water
		will be filtered on its way into and out of the tanks.</p>
<p>Our water tanks may be connected to the town water
	supply to ensure a minimum water level is maintained.&nbsp; We
	may also incorporate a device such as <a href="http://www.rainharvesting.com.au/rain_alert.asp" target="_blank">Rain
	Alert</a> to monitor water levels.</p>
<p>&nbsp;</p>
<h2>Water Recycling</h2>
<p>Each home will have a grey water recycler (e.g.
	a <a href="http://www.perpetualwater.com.au/site/howitworks1.php" target="_blank">Perpetual
	Water - Home</a>)
	built-in, which will recycle water to a level where
	it can be re-used in the washing machine or on
	the garden.&nbsp; Incoporating a system like
	this greatly reduces a household's water consumption.&nbsp; A
	second tank is used to store recycled grey water. </p>
<p>&nbsp;</p>
<h2>Dry Composting Toilet</h2>
<p>To save a large amount of water (flushing toilets
	accounts for about one third of a regular household's
	water usage) while also producing a high quality fertiliser
	for your permaculture garden, each home will include
	a dry composting toilet such as a <a href="http://www.rotaloo.com/" target="_blank">Rota-Loo</a>. </p>
<p>&nbsp;</p>
<h2>Energy-Efficient Hot Water System</h2>
<p>A heat pump hot water system such as the one by <a href="http://www.enviro-friendly.com/quantum-heat-pump-water-heater.shtml" target="_blank">Quantum</a> will
	be used, located under the house in the same space
	as the water tanks and dry toilet.&nbsp; These are
	even more energy efficient than solar hot water systems,
	and can produce hot water even if ambient temperatures
	drop as low as -15&deg;C. </p>
<p>&nbsp;</p>
<h2>Cold Water System</h2>
<p>This is a luxury item!&nbsp; As most Queenslanders
	know, tap water can become uncomfortably hot during
	summer months.&nbsp; Therefore we will offer an optional
	water cooler to be installed under the kitchen sink
	alongside the water filter.</p>
<p>&nbsp;</p>
<h2>Appliances</h2>
<p>We will provide the option to include an energy-efficient
	refrigerator and/or a water-efficient washing machine
	with each home.</p>
