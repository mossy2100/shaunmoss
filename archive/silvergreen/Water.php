<h1>Water and Wastewater</h1>
<p>Another key ambition for the ecovillage is
	water independence.&nbsp; The ecovillage will produce
	ample water for its needs, including high quality
	drinking water as well as water for gardening and
	other uses.</p>
<p>&nbsp;</p>
<h2>Rainwater Harvesting</h2>
<p>Our primary source of water will be rainwater harvesting.&nbsp; Each
	home, as well as the community centre, will have
	water tanks for storage of up to 3 months worth
	of water.&nbsp; The Samford area has been heavily
	drought affected, so we cannot rely on consistent
	rainfall throughout the year.</p>
<p>Gutter mesh and first flush diverters will be installed
	on all buildings where rainwater is harvested,
	in order to keep leaves, twigs, frogs, mosquitos
	and other muck out of the tanks.</p>
<p>&nbsp;</p>
<h2>Water Purification</h2>
<p>All drinking water on the site will be purified
	to a high standard, for optimal health.&nbsp; Homes
	will have a carbon filter, such as a <a href="http://purewater.net.au/whole_house.htm" target="_blank">Pure
	Water Systems &quot;whole of house&quot;</a> filter,
	that removes sediments, chlorine and other substances
	from the whole water supply to the house.&nbsp; This
	will be supplemented by an under-sink reverse
	osmosis filter in the kitchen that further purifies
	water for drinking and cooking.</p>
<p>We may also include one of the more modern technologies
	that affect the electromagnetic frequencies of
	water, e.g. <a href="http://www.grander.com.au/" target="_blank">Grander
	Water</a>, in order to improve
	water quality in the
	ecovillage.&nbsp; There are several options in this
	emerging technology, which need to be evaluated
	for price and effect.</p>
<p>&nbsp;</p>
<h2>Hot Water Systems</h2>
<p><a href="http://www.quantumenergy.com.au/" target="_blank">Quantum</a> heat
	pump hot water systems will be used in each home
	in the ecovillage, as these are currently the most
	energy-efficient method of heating water available
	on the market.&nbsp; The
	government also provides a rebate for each of these
	units installed.</p>
<p>&nbsp;</p>
<h2>Wastewater Re-use</h2>
<p>There are various options for grey and black
	water re-use, which are still being investigated.</p>
<p>The most water-efficient solution will use dry composting
	toilets (e.g. <a href="http://www.rotaloo.com/" target="_blank">Rota-Loo</a>)
	across the site to reduce black water, in conjunction
	with <a href="http://www.perpetualwater.com.au/site/howitworks1.php" target="_blank">Perpetual
	Water - Home</a> units that recycle grey water
	to tertiary levels.&nbsp; This is the more expensive
	setup (around $16,000 per home), but will provide
	a supply of good quality water that can be used
	on either above-ground or sub-surface irrigation,
	or for washing cars or clothes.&nbsp; In
	this setup, the only lost water will be
	from the kitchen sink.&nbsp; However, water for sub-surface
	irrigation does not need to be treated to tertiary
	levels, so this system may simply be overkill.</p>
<p>An alternative option is to use the Biolytix BF6
	wastewater treatment systems, an award-winning
	alternative to septic systems.&nbsp; These systems
	treat grey and black water to secondary levels,
	which means the recycled water can be used on sub-surface
	irrigation only.&nbsp; This may be acceptable since
	we will be using sub-surface irrigation, and waterless
	car washing systems such as <a href="http://www.nowetcarclean.com.au/" target="_blank">NoWet</a>.&nbsp; The
	benefits of this system is that it will be much
	cheaper, and it will recycle water from the kitchen.&nbsp; It
	also means that flushing toilets becomes a much
	more acceptable option, since this black water
	would be recycled.&nbsp; A Biolytix solution has recently
	been installed in <a href="http://www.somervilleecovillage.com.au/" target="_blank">SomerVille
	Ecovillage</a>.&nbsp; One disadvantage of this option
	is that the recycled grey water cannot be stored,
	but is flushed to the gardens daily.</p>
<p>&nbsp;</p>
<h2>Sub-surface Irrigation</h2>
<p>Large areas of the site will be allocated for permaculture
	gardens that will provide the ecovillage with
	its food supply (see the <a href="Food.php">Food</a> page).&nbsp; Sub-surface
	irrigation will be used in all these areas, which
	significantly reduces water use by delivering water
	directly to the plants' roots.&nbsp; This approach
	significantly mitigates water run-off, deep
	leaching, water loss to evaporation, and the creation
	of ponds or channels.</p>
<p>&nbsp;</p>
