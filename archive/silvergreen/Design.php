<h1>Silvergreen Design </h1>
<p>&nbsp;</p>
<h2 align="left">Location</h2>
<p align="left">The Samford Valley is a very beautiful
	location featuring good-natured people, lots of
	trees and wonderful, sweet-smelling fresh air.&nbsp; It
	has been selected mainly because of convenience
	to Brisbane.&nbsp; The
	largest blocks of land around Samford are mostly
	about 16-20 hectares (40-50 acres), which should
	be enough for a small ecovillage.</p>
<p align="left">The challenge of the Samford Valley
	is lack of rainfall - the dams around
	there are currently very low.&nbsp; Fortunately
	our ecovillage design will include all the water
	catchment and recycling technology needed to overcome
	this issue.</p>
<p>&nbsp;</p>
<h2 align="left">Size</h2>
<p>Brisbane is a popular and fast-growing city, and
	large blocks of land are difficult to locate.&nbsp; Samford
	Valley is a compromise - it is only about 20 minutes
	drive from the CBD, yet there are still a number of
	medium sized blocks that will be suitable for this
	development.&nbsp; Most available blocks of an acceptabe
	size in this region are around 16 hectares.&nbsp; Using
	a rule of thumb of one hectare per household, the
	project is expected to include 16 houses plus a community
	centre and other shared facilities.</p>
<p>While this is rather small compared with other ecovillages,
	we believe the project will be very unique, popular
	and worthwhile, and will hopefully inspire other similar
	developments at this scale.</p>
<p>The population for Silvergreen
will therefore only be around 50-60 people, ideally
	about half adults and half children.</p>
<p align="left">&nbsp;</p>
