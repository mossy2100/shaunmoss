<p align="left">As you can see, there is some considerable
	variation in size and population density, with
	an average area per household of about 1.5 ha.&nbsp; The
	last two ecovillages in the list represent the
	extremes.</p>
<p align="left">Silvergreen will therefore be on the
	higher density side, which is hard to avoid on
	a relatively small block of land close to the city,
	because land is more expensive and less available.&nbsp; Even
	so, house lots will be twice as large as a typical
	0.1 ha (&frac14; acre)
	suburban block, and in actual fact, residents will
	each own 1 hectare of land, which is 10 times the
	size of a suburban block.&nbsp; Since capital growth
	comes primarily from appreciation of land value,
	the residents will be well off financially - this
	will be a great investment. &nbsp; </p>
<p align="left">For the sake of a preliminary design,
	we are working with the following figures:</p>
<ul>
	<li>20% of land allocated for housing (3.2 ha)</li>
    <li>30% for
    		common land - that's community buildings,
    	roads, shared infrastructure, permaculture
    	gardens and farmland (4.8 ha)</li>
    <li>50%
    		natural and rehabilitated (8 ha)</li>
</ul>
