<h1>Task Groups </h1>
<p>Our task groups are organised around the triple-bottom-line
	outcomes of environmental, social and economic
	sustainability.</p>
<p>The <strong>Design</strong> task group is responsible
	for achieving <strong>environmental</strong> sustainability
	outcomes.&nbsp; Their main roles include designing
	the water, energy, food (permaculture) and recycling
	systems, the homes, the community centre, and other
	design-related items.&nbsp; They will also be responsible
	for producing the &quot;Development Code&quot; for
	the ecovillage.&nbsp; Science, engineering, IT
	and permaculture design knowledge will be useful
	in this group. </p>
<p>The <strong>Community</strong> task group is responsible
	for achieving <strong>social</strong> sustainability
	outcomes.&nbsp; Their
	function will include organising meetings and group
	activities, promoting the ecovillage, building
	the group, facilitating consensus decision-making
	processes, conflict resolution, and communications
	(including internet/web).&nbsp; Knowledge
	of community processes, psychology, sociology,
	marketing and communications will be useful in
	this group.</p>
<p>The <strong>Real Estate and Finance</strong> task
	group is responsible for <strong>economic</strong> sustainability
	outcomes.&nbsp; Their
main roles include researching real estate and zoning,
	finding and securing appropriate land, setting
	up legal entities, and researching financing options
	and business models for the ecovillage.&nbsp; Knowledge
	of accounting, law, real estate, finance, taxation
	and investing will be useful in this group. </p>
<p>Anyone can join any group.&nbsp; Each group will
	have tasks and deliverables allocated to it, and
	will be required to present the results of
	their research and creativity to the rest of the
	community at meetings.</p>
