<h1>Recycling</h1>
<p>Recycling will be a major sustainability feature
of the ecovillage.</p>
<p>This will be done in co-operation with council as
much as possible.&nbsp; The homes will have bins for
each type of rubbish, e.g. paper, cans and bottles,
food scraps, and other items.&nbsp; Cans, bottles and
paper products will probably go to council, and food
scraps given to the chooks.</p>
<p>Items such as scrap metal, IT equipment,
	whitegoods, tyres, wood, plastic, etc., can be
	taken to the recycling centre, which will be a small
	building on-site adjacent to the workshop.&nbsp; Items
	will be sorted into piles, and residents can help
	themselves.&nbsp; Then,
	once a month, an industrial recycling company such
	as
	<a href="http://www.sims-group.com/recycling/home/default.asp" target="_blank">Sims</a> will
	come and collect all the items that nobody wants.</p>
<p>Our intent is that <em>all</em> unwanted
	materials and items on the site will be properly
	and sustainably recycled.</p>
<p>&nbsp;</p>
<h2>Paper and wood </h2>
<p>A major part of the philosophy underlying Silvergreen
	is that trees are considerably more valuable when
	alive, since they remove carbon from the
	atmosphere and provide us with healthy oxygen.&nbsp; Hence,
	we seek to minimize the use of wood and paper on-site
	as much as we can, which is part of the
	reason why Silvergreen's homes are largely constructed
	from steel.</p>
<p>To reduce paper usage, we will encourage the concept
	of a &quot;paperless community&quot;, made easier by the
	fact that the whole community will be networked.&nbsp; Right
	from the beginning, almost all communications about
	this project will be electronic.</p>
<p>&nbsp;</p>
