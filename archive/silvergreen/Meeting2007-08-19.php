<h2>Results</h2>
<p>The meeting was attended by 8 people: Shaun, Russell
	&amp; wife Gabrielle, Mike and his cousin Tom, Ewen,
	Neisha and Helen.</p>
<p>This was a great cross-section
	of people, which held very positive and interesting
	overtones for the future of the community.&nbsp; Russell
	and Gabrielle have the most experience in ecovillage
	creation, being involved in an earlier project
	known as the Brisbane Ecovillage.&nbsp; Russell is
	also a founding member of ECNA (the <a href="http://www.ecna.org.au/drupal" target="_blank">Ecovillage
	and Cohousing Network of Australia</a>), which links
	us from an early stage with this organisation,
	and therefore the Global Ecovillage Network of
	which it is a component.&nbsp; Among the attendees
	were elements of spirituality, engineering, science,
	IT, organisational and people management skills,
	and experience with villages and co-housing communities.</p>
<p><a href="doc/Silvergreen%20-%20Meeting%201%20-%20Design%20Worksheet.doc">This
		worksheet</a> was used as a very basic tool for
	learning people's ideas and desires with regard
	to being part of an ecovillage.&nbsp; The results
	from the worksheet are as follows:</p>
<p>&nbsp; </p>
<h3>What would interest you about an ecovillage?</h3>
<p>This is a very important question because it links
	to our principles and vision, which are the next steps
	in our <a href="Process.php">process</a>.</p>
<p>Everyone agreed on &quot;reducing ecological footprint&quot;,
	so there is a clear environmentalist streak in our
	group.&nbsp; The second most popular reason was &quot;to
	be part of a community&quot;, followed by &quot;survival&quot;.</p>
<p>Being &quot;close to nature&quot; and &quot;health
	benefits&quot; ranked equal as the 4th and 5th main
	reasons.&nbsp; &quot;Reduced cost of living&quot; and &quot;building
	own sustainable house&quot; ranked equal 6th and 7th.</p>
<p>&quot;Good investment&quot; was by far the least
	important reason.</p>
<p>Reduce ecological footprint: 7<br>
	To be part of a community: 6.5<br>
	Survival: 6<br>
	Close to nature: 5.5<br>
	Health benefits: 5.5<br>
	Reduce cost of living: 5<br>
	Build own sustainable house: 5<br>
	To participate in conservation: 4.5<br>
	Security: 4.5<br>
	Access to community centre: 4.5<br>
	Good investment: 2 </p>
