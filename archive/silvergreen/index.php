<h1>Welcome to Silvergreen Ecovillage</h1>
<p>&nbsp;</p>
<h2>Overview</h2>
<p>Silvergreen is a new, modern ecovillage to be located
	near Brisbane, the capital city of Queensland,
	Australia.&nbsp; The
	goal for this development is to introduce a high-tech
	style of ecovillage, and show that sustainable
	living can be affordable, comfortable and fashionable.&nbsp; We
	hope that by setting an example, this style of
	development will become the rule rather than the exception
	in South East Queensland, and ultimately the world.</p>
<p>The project is currently in the concept/planning
	stage, and as yet no land has been purchased.&nbsp; We
	are currently building up a group of interested
	people and potential residents in order to refine
	the design of the community and to create momentum
	for the project.</p>
<p>Simultaneously, we are developing
		documents to describe our process, principles,
		and vision; process being the current
	main priority. </p>
<p>Although some design details are described on this
	website, this has really only been done as a starting
	point.&nbsp; We
	are in the very early stages of this project and
	nothing
	is carved in stone.</p>
<p>In general, we agree that the site should support
	at least 100 households, and should be within one
	hour's drive of Brisbane.</p>
<p>&nbsp;</p>
<h2>Features</h2>
<p>The site will ideally be self-sufficient in energy,
	water and food.&nbsp; Energy may be generated approximately
	50% from solar power and 50% from wind, in a grid-connected
	system.&nbsp; Water will be produced from rainwater
	harvesting and purification, and will be recycled
	on site.&nbsp; Food is likely to be produced from
	a combination of permaculture, aquaculture and poultry
	farming.</p>
<p>The homes will be highly sustainable and durable,
	constructed from solid steel frames and sustainably
	produced plantation timber.&nbsp; The designs incorporate
	passive cooling and heating in order to reduce
	energy costs, and may have a style similar to
	the traditional Queenslander, with open spaces
	and wide decks.&nbsp; They
	will incorporate a range of water and energy saving
	features, along with a computer to track usage
	of these resources.</p>
<p>The community centre may include features such as
	food storage facilities, a commercial
	kitchen, a cafe/restaurant that will double as
	a meeting place, an office, games
	room, reading room/library, and swimming pool.&nbsp; Other
	community facilities will include farm buildings,
	a workshop and a recycling centre.</p>
<p>Wireless networking and internet will
	be integrated seamlessly into all homes and the
	community centre.&nbsp; One goal for the site is to
	minimize paper use as much as possible, and most
	of the documentation used by this project will
	remain in electronic format.</p>
<p align="left">The homes, community centre and other
	buildings may possibly all be built by the
	same builder, in order to maintain a consistent
	aesthetic theme across the ecovillage, to reduce
	costs, and to improve systems integration.</p>
