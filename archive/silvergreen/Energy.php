<h1>Energy</h1>
<p>A major goal for the ecovillage is energy
	independence.&nbsp; In other words, the ecovillage
	should produce enough energy for its own needs.</p>
<p>&nbsp;</p>
<h2>Solar and Wind Energy </h2>
<p>At present it is expected that about 50% of this
	energy production will come from solar power, and
	about 50% from wind power.</p>
<p>Both solar and wind offer certain advantages and
	disadvantages.&nbsp; Neither solar nor wind will produce
	electricity all the time, simply because the sun
	is not always shining and the wind is not always
	blowing.&nbsp; However,
	solar and wind make an excellent combination, since
	it is often windier when
	the sun is not shining, such as at nighttime or
	during storms.&nbsp; Using both technologies together
	means a more consistent supply of electricity during
	a 24-hour period.</p>
<p>Solar energy is more reliable than wind, since on
	most days (especially in Queensland) the sun will
	shine for about 12 hours.&nbsp; However, although
	wind is less reliable, it offers the advantage
	of being much cheaper.</p>
<p>The solar panels will be positioned on the roofs
	of the homes, the community centre, and other buildings.&nbsp; The
	wind turbines will be located in the most windy
	(probably the highest) places on the site.</p>
<p>&nbsp;</p>
<h2>Grid Connection </h2>
<p>The community will be connected to the main electricity
	grid via an inverter, so that we will not need
	an energy storage solution (i.e. batteries) on
	site.&nbsp; This is good for the environment because
	batteries are hard to recycle, besides being costly.</p>
<p>A grid-connected system will mean that when the
	ecovillage is not producing enough electricity
	for its needs (e.g. when it is neither sunny nor
	windy), then electricity can be drawn from the
	main grid in the usual way.&nbsp; It also means that
	when the ecovillage is producing an excess of
	energy, this excess will be returned to the grid.</p>
<p>The ecovillage will only be billed by Origin (or
	whatever energy company) for the balance.&nbsp; If
	the energy system is well designed, then more often
	than not we will actually return more electricity
	to the grid than we use, in which case the ecovillage
	will receive payment from Origin for this surplus
	energy.</p>
<p>&nbsp;</p>
<h2>Energy Saving</h2>
<p>By designing the community for low energy usage,
	we greatly reduce the cost of construction, since
	solar panels and wind turbines are reasonably expensive
	at present.</p>
<p>One way that energy requirements can be significantly
	reduced is through passive heating and cooling
	solutions.</p>
<p>Designing for passive heating means
	positioning rooms such as the lounge room or home
	office on the north side of the house, since these
	often have people in them during the day, and it
	is the north side of the house which receives the
	most sunlight.&nbsp; This reduces the cost of heating
	in winter.&nbsp; Similarly, rooms such as a bedrooms,
	bathrooms and laundry can be located on the south
	side of the house, since these rooms are generally
	not occupied very much during the day, so it does
	not matter if they are cooler. </p>
<p>Designing for passive cooling means setting
	up breezeways so that air can flow through or under
	the house, in order to keep the house cool in summer.&nbsp; This
	is why many Australian homes are on posts, so that
	air can blow under the house.&nbsp; More modern designs
	use louvres and large openings to allow breezes
	to flow right through the home.</p>
<p>Silvergreen's homes will also include energy-saving
	light bulbs, flat-panel TV screens and computer
	monitors, heat pump hot water systems, energy-efficient
	refrigerators, and other energy-saving appliances
	and devices wherever possible.&nbsp; We may also
	include devices that ensure that TVs and
	DVD players do not sit on standby but switch to
	fully off, and occupancy sensors that automatically
	turn lights off when there is no-one in a room.</p>
<p>While an average suburban home requires about 5kW,
	with good design it should be possible to reduce
	this to around 3.5kW.&nbsp; This will greatly reduce
	the cost of our energy production system and be
	a good example of sustainable design. </p>
<p>&nbsp;</p>
