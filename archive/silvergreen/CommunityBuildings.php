<h2>Community Centre</h2>
<p>The Silvergreen Community Centre will sit at the
	hub of the ecovillage, and will function as the
	central meeting point and &quot;hub of operations&quot;.</p>
<p>It will include:</p>
<ul>
	<li>Large cold-room for food storage.</li>
    <li>Commercial kitchen</li>
    <li>Open dining area with AV equipment, which can
    	be used for meals, meetings, parties, presentations,
    	seminars, movies, etc.&nbsp; Some outdoor tables. </li>
    <li> Relaxation, reading and games room with beanbags,
    	books, pool table, ping pong table and pinball
    	machine.</li>
    <li>Office with phone, photocopier, printer, scanner,
    	and the community web server and internet gateway.</li>
    <li>Bathroom facilities with waterless toilets
    	and urinals.</li>
</ul>
<p>&nbsp;</p>
<h2>The Shed</h2>
<p>&quot;The Shed&quot; will be a steel industrial building comprised
	of 3 main sections:</p>
<ol>
	<li>Farm equipment storage.&nbsp; This is where all
		gardening tools, fertiliser, seeds, the tractor,
		and any other equipment needed for maintaining
		the grounds is kept.</li>
    <li>The workshop.&nbsp; This will be stocked with
    	 all kinds of tools, including welding
    	gear.&nbsp; The
    	workshop will be used for repairs to any equipment,
    	including IT equipment, or for manufacturing/inventing. </li>
    <li>The recycling centre.&nbsp; Any items such
    	as old computer equipment or whitegoods, scrap
    	metal or wood, etc. is brought here for re-use
    	or eventual recycling.&nbsp; See the <a href="Recycling.php">Recycling</a>    	page
    	for more information.</li>
</ol>
