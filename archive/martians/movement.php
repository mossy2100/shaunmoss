<h2>Movement</h2>

<p>Players can move around Mars in several ways (modes). Each mode has a different user interface,
and variations in speed and dynamics.</p>

<ol type='a'>
	<li>Walking around in a pressurized area, such as a building or habitat module. This does not
	require a marssuit. It is like walking around on Earth except for the reduced gravity.<br>
	<br>
	
	
	<li>Walking around outside in the open Martian air. This naturally requires a marssuit. The view is
	through the faceplate of the suit and shows the suit's O<sub>2</sub> and H<sub>2</sub>O supplies,
	battery power, interior temperature and integrity (i.e. if it's torn or otherwise damaged)<br>
	<br>
	
	
	<li>Driving around on an ATV (All-Terrain Vehicle - like a motorized tricycle) or any other kind of
	unpressurized rover. This also requires a marssuit, and has the same view as Marssuit except that
	fuel/battery level is also shown.<br>
	<br>
	
	
	<li>Driving around in a pressurized rover. The view shown is that through the windscreen - a
	marssuit is not required within a pressurized rover. The UI shows O<sub>2</sub>, H<sub>2</sub>O,
	food and fuel/battery levels of the rover, as well as interior temperature and integrity (i.e. if
	the rover is damaged)<br>
	<br>
	
	
	<li>Flying around in a plane/glider/balloon, etc. Providing the best views of Mars! Similar to a
	pressurized rover in terms of UI, although controls may be more complex.<br>
	<br>

</ol>

