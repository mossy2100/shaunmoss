<h2>Martian Chemistry</h2>

<p>This page will serve as an introduction to Martian chemistry.  If you want to live on Mars, it's necessary to learn at least the basics.

<h4>Atmosphere</h4>

<p>Mars's atmosphere is primarily carbon dioxode (CO<sub>2</sub>), which is toxic to humans in high concentrations.  The air pressure is only 7 mbars, as compared with about 1000 mbars on Earth.  So, the air is very thin, but can also reach very high speeds.

<p>For humans or other animals to live on Mars, we need to have oxygen (O<sub>2</sub>) to breathe.  The carbon dioxide atmosphere can be converted to oxygen by removing the carbon atom from the molecule.  This is done using units called "atmosphere scrubbers".  Small scrubbers are used to recycle exhaled air inside a hab module or rover - large scrubbers are able to take in Martian air and output oxygen, which is usually stored in tanks and sold.

<h4>ISRU Plants</h4>

<p>ISRU means "In-Situ Resource Utilization".  This refers to making use of Mars's own resources (such as air and dirt) instead of importing everything from Earth.  Mars has an atmosphere with CO<sub>2</sub> and trace elements, and plenty of ferrous oxide (Fe<sub>2</sub>O<sub>3</sub>) in the regolith.  Elements extracted from these sources can be combined with imported materials to form useful molecules.

<p>A standard ISRU, as originally visualized by Roberty Zubrin, takes CO<sub>2</sub> from the atmosphere and combines it with hydrogen gas (H<sub>2</sub>) imported from Earth.  This produces oxygen (O<sub>2</sub>), water (H<sub>2</sub>O) and methane (CH<sub>4</sub>).  Oxygen and water are needed by the colonists, and methane can be used as rover fuel.  An alternative process can produce methanol (CH<sub>3</sub>OH) which is also a useful fuel.

<p>A different kind of ISRU plant might take regolith (dirt) as input, which contains a large percentage of ferrous oxide, combines it with carbon dioxide from the air, and produce oxygen and steel, an important building material.  Steel is commonly made from iron combined with carbon.  Other metals extracted from the dirt, such as magnesium, titanium or aluminium can be mixed in to form different useful kinds of steel or alloys.

<h4>Water</h4>

<p>It appears as if water once flowed in large volumes on the surface of Mars.  There may still be pockets of liquid water in warm areas below the surface, however it is generally believed that most of Mars's waters is either frozen at the poles or in the regolith, forming permafrost.

<p>If water can be accessed in large columes on Mars, this will alleviate the requirement to import hydrogen from Earth.  Water molecules can be split into hydrogen and oxygen.  The oxygen can be used for breathable air, and the hydrogen can be combined with the CO<sub>2</sub> from the atmosphere to form methane and more oxygen.


</p>



