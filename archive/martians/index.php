<h2>Overview</h2>

<p><b>Martians</b> is an online computer game in which players (the Martians) colonize Mars.  Players can do many things including operating businesses, conducting research, exploring, learning about Mars, or engaging in discussions about how Mars should or would be colonized.

<p>The game environment is a huge shared virtual environment - an accurate 3D model of Mars constructed using real topographical data.  Players move around in first-person mode - they can buy and sell, enter discussions or chat channels, build bases, explore Mars in a marssuit or vehicle.

<p>One major aspect of the game is how players decide together how Mars should be developed.  They are encouraged to form their own government and decision-making processes.  Decisions about how Mars or the game should be developed are made by the players - this directs the development team.

<p>It is not only a game but also an online community.  The game is Open Source and developed co-operatively by developers and players around the world.


<h4>Key Features</h4>

<ol type=1>
<li>The setting is Mars 50 years in the future.  Players are early colonists, tourists, explorers, entrepreneurs, etc.<br><br>

<li>The game takes place in a huge 3D shared virtual environment which models Mars accurately using topographical data from the MOLA (Mars Orbiting Laser Altimeter).  This model changes over time as bases are built on Mars by the players.<br><br>

<li>The main part of the interface is a 3D first-person view which enables players to interact with the game environment, and with each other. <br><br>

<li>The game provides accurate physics and environment simulation, for example, Martian gravity (0.38g); thin CO<sub>2</sub> atmosphere; weather (dust storms, clouds, etc.).<br><br>

<li>The game requires players to think about their safety, in terms of protection from the native Martian environment, and access to oxygen, water and food.<br><br>

<li>Players can move about in the environment within certain limitations imposed by the simulated environment.  For example, the atmosphere of Mars is not breathable, therefore a marssuit is required to move around outside.  A variety of vehicles are also available.<br><br>

<li>Primary activities within the game environment are:
	<ol type=a>
	<li>base-building
	<li>commerce and trade
	<li>communication and relationship-building
	<li>development of government and law
	<li>exploration
	</ol>
</ol>

