<h2>User Interface</h2>

<p>A very basic sketch, simply to show the main elements:

<p><img src='images/ui.jpg' width=750 height=500>

<p>As discussed, the primary element of the user interface is a 3D first-person view of Mars.

<p>A mini-browser is provided, which can be used for a variety of purposes:
<ol type=a>
<li>to show a player's resources or other personal info
<li>to provide online help about how to play the game
<li>to provide an encyclopaedia about Mars
<li>to show maps, diagrams, etc.
<li>to show special announcements or game news
<li>to show emergency warnings, such as a suit breach or a rover malfunction
<li>to convert Earth money to Mars money using their credit card
</ol>

<p>A "head-up" display is provided, which shows different information depending on what mode of transport the player is using (see Movement).  This display could be integrated with the 3D view (above, it is in a separate section in the top-left).

<p>The current date, time, and location of the player is always shown - this is assumed to be part of the wristpad which the player always has on

</p>

