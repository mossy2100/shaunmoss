<h2>Government and Law</h2>

<p>As evidenced by the <i>Republic of Mars</i>, there is interest in creating a unique Martian government.  This is something which should be designed completely by the colonists.  Players can also design their own laws for Mars.  As far as possible, they can implement these themselves, but when necessary these ideas may be implemented by the game developers.  In fact, the players themselves collectively direct, or at least guide, the developers as far as what directions the game should evolve in.

<p>As with ROM, government and laws can be designed simply by players talking with each other in the discussion forums.  If they have anything related to laws which needs to be implemented by the developers, these can be submitted to the development team.

</p>


